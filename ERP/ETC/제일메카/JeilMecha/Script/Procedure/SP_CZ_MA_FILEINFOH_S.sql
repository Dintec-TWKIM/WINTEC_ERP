ALTER PROCEDURE [dbo].[SP_CZ_MA_FILEINFOH_S] 
(
	@P_ODR_NO	NVARCHAR(20),
	@P_DT_FROM	NVARCHAR(8),
	@P_DT_TO	NVARCHAR(8)
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT ODR_NO
FROM CZ_MA_FILEINFO
WHERE DT_INPUT BETWEEN @P_DT_FROM AND @P_DT_TO 
AND (ISNULL(@P_ODR_NO, '') = '' OR ODR_NO LIKE '%' + @P_ODR_NO + '%')
GROUP BY ODR_NO

GO