ALTER PROCEDURE [dbo].[SP_CZ_MA_FILEINFOL_S] 
(
	@P_ODR_NO	NVARCHAR(20)
)
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT SEQ,
	   ODR_NO,
	   TP_FILE,
	   LEFT(DT_INPUT, 4) + '-' + SUBSTRING(DT_INPUT, 5, 2) + '-' + RIGHT(DT_INPUT, 2) AS DT_INPUT,
	   FILE_NAME
FROM CZ_MA_FILEINFO
WHERE ODR_NO = @P_ODR_NO

GO