ALTER PROCEDURE [NEOE].[SP_CZ_MA_TARIFF_DHL_STD_L_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_MA_TARIFF_DHL_STD_L A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY	NVARCHAR(7),
			  DT_YEAR		NVARCHAR(4),
			  TP_TARIFF		NVARCHAR(4),
			  WEIGHT		NUMERIC(18, 2)) B 
ON A.CD_COMPANY = B.CD_COMPANY 
AND A.DT_YEAR = B.DT_YEAR
AND A.TP_TARIFF = B.TP_TARIFF
AND A.WEIGHT = B.WEIGHT
-- ================================================== INSERT
INSERT INTO CZ_MA_TARIFF_DHL_STD_L 
(
	CD_COMPANY,
	DT_YEAR,
	TP_TARIFF,
	WEIGHT,
	AM_ZONE1,
	AM_ZONE2,
	AM_ZONE3,
	AM_ZONE4,
	AM_ZONE5,
	AM_ZONE6,
	AM_ZONE7,
	AM_ZONE8,
	AM_ZONE9,
	ID_INSERT,
	DTS_INSERT
)
SELECT CD_COMPANY,
	   DT_YEAR,
	   TP_TARIFF,
	   WEIGHT,
	   AM_ZONE1,
	   AM_ZONE2,
	   AM_ZONE3,
	   AM_ZONE4,
	   AM_ZONE5,
	   AM_ZONE6,
	   AM_ZONE7,
	   AM_ZONE8,
	   AM_ZONE9,
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
  FROM OPENXML (@DOC, '/XML/I', 2) 
          WITH (CD_COMPANY	NVARCHAR(7),
			    DT_YEAR		NVARCHAR(4),
				TP_TARIFF	NVARCHAR(4),
			    WEIGHT		NUMERIC(18, 2),
				AM_ZONE1	NUMERIC(18, 0),
				AM_ZONE2	NUMERIC(18, 0),
				AM_ZONE3	NUMERIC(18, 0),
				AM_ZONE4	NUMERIC(18, 0),
				AM_ZONE5	NUMERIC(18, 0),
				AM_ZONE6	NUMERIC(18, 0),
				AM_ZONE7	NUMERIC(18, 0),
				AM_ZONE8	NUMERIC(18, 0),
				AM_ZONE9	NUMERIC(18, 0))
-- ================================================== UPDATE    
UPDATE A 
   SET A.AM_ZONE1 = B.AM_ZONE1,
	   A.AM_ZONE2 = B.AM_ZONE2,
	   A.AM_ZONE3 = B.AM_ZONE3,
	   A.AM_ZONE4 = B.AM_ZONE4,
	   A.AM_ZONE5 = B.AM_ZONE5,
	   A.AM_ZONE6 = B.AM_ZONE6,
	   A.AM_ZONE7 = B.AM_ZONE7,
	   A.AM_ZONE8 = B.AM_ZONE8,
	   A.AM_ZONE9 = B.AM_ZONE9,
	   A.ID_UPDATE = @P_ID_USER, 
	   A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
  FROM CZ_MA_TARIFF_DHL_STD_L A 
  JOIN OPENXML (@DOC, '/XML/U', 2) 
          WITH (CD_COMPANY	NVARCHAR(7),
			    DT_YEAR		NVARCHAR(4),
				TP_TARIFF   NVARCHAR(4),
			    WEIGHT		NUMERIC(18, 2),
				AM_ZONE1	NUMERIC(18, 0),
				AM_ZONE2	NUMERIC(18, 0),
				AM_ZONE3	NUMERIC(18, 0),
				AM_ZONE4	NUMERIC(18, 0),
				AM_ZONE5	NUMERIC(18, 0),
				AM_ZONE6	NUMERIC(18, 0),
				AM_ZONE7	NUMERIC(18, 0),
				AM_ZONE8	NUMERIC(18, 0),
				AM_ZONE9	NUMERIC(18, 0)) B 
  ON A.CD_COMPANY = B.CD_COMPANY
  AND A.DT_YEAR = B.DT_YEAR
  AND A.TP_TARIFF = B.TP_TARIFF
  AND A.WEIGHT = B.WEIGHT

EXEC SP_XML_REMOVEDOCUMENT @DOC 

GO