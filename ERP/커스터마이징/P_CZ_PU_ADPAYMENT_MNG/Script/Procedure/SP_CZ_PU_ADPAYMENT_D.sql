ALTER PROCEDURE [NEOE].[SP_CZ_PU_ADPAYMENT_D] 
(
	@P_NO_ADPAY    NVARCHAR(20),
	@P_CD_COMPANY NVARCHAR(7)
) 
AS 

BEGIN
	DECLARE @P_ERRMSG VARCHAR(255) -- ERROR 메시지

	DELETE PU_ADPAYMENT 
	WHERE CD_COMPANY = @P_CD_COMPANY 
	AND NO_ADPAY = @P_NO_ADPAY 

	IF (@@ERROR <> 0) 
	BEGIN 
		SELECT @P_ERRMSG = '[SP_CZ_PU_ADPAYMENT_D]작업을 정상적으로 처리하지 못했습니다.'
		GOTO ERROR 
	END 

	RETURN 

	ERROR: 
	RAISERROR (@P_ERRMSG,18,1) 
END
   
GO

