																								 AND PL.NO_SO =GL.NO_SO
																								 AND PL.NO_SOLINE = GL.SEQ_SO
																								 FOR XML PATH('')),1,1,'') END) AS NM_SUPPLIER
	FROM SA_GIRL GL               
	JOIN SA_GIRH GH ON GH.CD_COMPANY = GL.CD_COMPANY AND GH.NO_GIR = GL.NO_GIR      
