		   GH.DT_GIR,
		   GH.CD_PARTNER, 
		   MP.LN_PARTNER, 
		   GH.CD_PLANT, 
		   GH.NO_EMP, 
		   ME.NM_KOR, 
		   GH.TP_BUSI, 
		   ISNULL(GH.YN_RETURN, 'N') AS YN_RETURN,
		   GD.CD_MAIN_CATEGORY, 
		   GD.CD_SUB_CATEGORY, 
		   GD.YN_PACKING, 
		   GD.YN_TAX_RETURN,
		   GD.CD_DELIVERY_TO, 
		   MP2.LN_PARTNER AS LN_DELIVERY_TO,
		   GD.SEQ_DELIVERY_PIC, 
		   GD.CD_FORWARDER,
		   GD.CD_FREIGHT, 
		   GD.WEIGHT, 
		   GD.DT_START, 
		   GD.DT_COMPLETE,
		   GD.YN_AUTO_SUBMIT,
		   GD.DT_AUTO_COMPLETE, 
		   GD.DT_BILL,
		   GD.FG_REASON, 
		   MH.NO_IMO, 
		   MH.NO_HULL, 
		   MH.NM_VESSEL,
		   GD.NO_IMO_BILL,
		   MH1.NO_HULL AS NO_HULL_BILL,
		   FA.AM_PRICE AS AM_FORWARDER_A,
		   FA.NM_FORWARDER AS NM_FORWARDER_A,
		   FB.AM_PRICE AS AM_FORWARDER_B,
		   FB.NM_FORWARDER AS NM_FORWARDER_B,
		   FC.AM_PRICE AS AM_FORWARDER_C,
		   FC.NM_FORWARDER AS NM_FORWARDER_C,
