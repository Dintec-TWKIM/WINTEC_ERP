		   GD.YN_PACKING, 
		   GD.YN_TAX_RETURN,
		   GD.CD_DELIVERY_TO, 
		   MP2.LN_PARTNER AS LN_DELIVERY_TO,
		   GD.SEQ_DELIVERY_PIC, 
		   GD.CD_FORWARDER,
		   GD.CD_FREIGHT, 
		   GD.WEIGHT, 
		   GD.DT_START, 
		   GD.DT_COMPLETE,
		   GD.YN_AUTO_SUBMIT,
		   GD.DT_AUTO_COMPLETE, 
		   GD.DT_BILL,
		   GD.FG_REASON, 
		   MH.NO_IMO, 
		   MH.NO_HULL, 
		   MH.NM_VESSEL,
		   GD.NO_IMO_BILL,
		   MH1.NO_HULL AS NO_HULL_BILL,
		   FA.AM_PRICE AS AM_FORWARDER_A,
		   FA.NM_FORWARDER AS NM_FORWARDER_A,
		   FB.AM_PRICE AS AM_FORWARDER_B,
		   FB.NM_FORWARDER AS NM_FORWARDER_B,
		   FC.AM_PRICE AS AM_FORWARDER_C,
		   FC.NM_FORWARDER AS NM_FORWARDER_C,
		   GD.CD_RMK,
		   GD.TP_CHARGE,
		   GD.DC_RMK, 
		   GD.DC_RMK1, 
		   GD.DC_RMK2,
		   GD.DC_RMK3,
		   GD.DC_RMK4,
		   GD.DC_RMK5,

		   NULL AS NM_DELIVERY,
		   NULL AS DC_DELIVERY_ADDR,
		   NULL AS DC_DELIVERY_TEL,
		   NULL AS DC_DESTINATION,
		   'N' AS YN_REVIEW,
		   'N'  AS YN_SHIPPING,
		   'N'  AS YN_CI,
		   'N'  AS YN_RECEIPT,
		   'N'  AS YN_REFUND,
		   NULL AS NO_GIR_SG,
		   'N'  AS YN_LOADING,
		   NULL AS DC_RMK_ADD,
		   'N'  AS YN_DOMESTIC,
		   NULL AS CD_SHIP_LOCATION,
		   'N'  AS YN_PRE_PHOTO,
		   'N'  AS YN_POST_PHOTO,
		   NULL AS DC_PHOTO,
		   'N'  AS YN_SPEC_CHECK,
		   NULL AS DC_SPEC,
		   'N'  AS YN_SEPARATE_PACK,
		   NULL AS DC_SEPARATE,
		   'N'  AS YN_CERT,
		   'N'  AS YN_UNIDENTIFIABLE,
		   NULL AS NO_FILE,
		   NULL AS TM_DELIVERY,
		   NULL AS DC_RMK_PACK,
		   NULL AS CD_PORT,
		   NULL AS DTS_ETB,
		   NULL AS DTS_ETD,
		   NULL AS DC_AGENCY,
		   NULL AS DC_RMK_SHIP,
		   'N' AS YN_UNPACK,
		   NULL AS DC_UNPACK,
		   NULL AS DTS_DEADLINE,
		   'N' AS YN_ETC,
		   'N' AS YN_EDIT,
		   NULL AS DC_VESSEL,
		   NULL AS DC_SHIP_CRANE
	FROM CZ_SA_GIRH_LOG GH            
	JOIN (SELECT GL.CD_COMPANY, GL.TP_GIR, GL.NO_GIR, GL.NO_HST, TI.NO_INV
		  FROM CZ_SA_GIRL_LOG GL
		  LEFT JOIN CZ_TR_INVL_LOG TI ON TI.CD_COMPANY = GL.CD_COMPANY AND TI.NO_INV = GL.NO_INV AND TI.NO_LINE = GL.SEQ_GIR
		  GROUP BY GL.CD_COMPANY, GL.TP_GIR, GL.NO_GIR, GL.NO_HST, TI.NO_INV) GL 
	ON GH.CD_COMPANY = GL.CD_COMPANY AND GH.TP_GIR = GL.TP_GIR AND GH.NO_GIR = GL.NO_GIR AND GH.NO_HST = GL.NO_HST
	LEFT JOIN CZ_SA_GIRH_DETAIL_LOG GD ON GD.CD_COMPANY = GH.CD_COMPANY AND GD.TP_GIR = GH.TP_GIR AND GD.NO_GIR = GH.NO_GIR AND GD.NO_HST = GH.NO_HST     
	LEFT JOIN CZ_MA_HULL MH ON MH.NO_IMO = GD.NO_IMO    
	LEFT JOIN CZ_MA_HULL MH1 ON MH1.NO_IMO = GD.NO_IMO_BILL    
	LEFT JOIN MA_PARTNER MP ON GH.CD_COMPANY = MP.CD_COMPANY AND GH.CD_PARTNER = MP.CD_PARTNER   
	LEFT JOIN CZ_MA_DELIVERY MP2 ON MP2.CD_COMPANY = GD.CD_COMPANY AND MP2.CD_PARTNER = GD.CD_DELIVERY_TO         
	LEFT JOIN MA_EMP ME ON GH.CD_COMPANY = ME.CD_COMPANY AND GH.NO_EMP = ME.NO_EMP
	LEFT JOIN (SELECT * FROM CZ_SA_GIRH_FORWARDER WHERE CD_COMPANY = @P_CD_COMPANY AND NO_GIR = @P_NO_GIR AND CD_FORWARDER = '001') FA ON FA.CD_COMPANY = GH.CD_COMPANY AND FA.NO_GIR = GH.NO_GIR
	LEFT JOIN (SELECT * FROM CZ_SA_GIRH_FORWARDER WHERE CD_COMPANY = @P_CD_COMPANY AND NO_GIR = @P_NO_GIR AND CD_FORWARDER = '002') FB ON FA.CD_COMPANY = GH.CD_COMPANY AND FA.NO_GIR = GH.NO_GIR
	LEFT JOIN (SELECT * FROM CZ_SA_GIRH_FORWARDER WHERE CD_COMPANY = @P_CD_COMPANY AND NO_GIR = @P_NO_GIR AND CD_FORWARDER = '004') FC ON FA.CD_COMPANY = GH.CD_COMPANY AND FA.NO_GIR = GH.NO_GIR
	WHERE GH.CD_COMPANY = @P_CD_COMPANY            
	AND GH.TP_GIR = '001'
	AND	GH.NO_GIR = @P_NO_GIR    
	AND GH.NO_HST = @P_NO_HST        
	ORDER BY GH.NO_GIR
	
	SELECT 'N' AS S,
		   GH.CD_PLANT, 
		   GL.SEQ_GIR,
		   GL.SEQ_SO,
		   TI.NO_LINE,
		   GL.NO_SO,
		   QL.NO_DSP,
		   ISNULL(QL.NM_SUBJECT, '') AS NM_SUBJECT,
		   ISNULL(QL.CD_ITEM_PARTNER, '') AS CD_ITEM_PARTNER,
		   ISNULL(QL.NM_ITEM_PARTNER, '') AS NM_ITEM_PARTNER,
		   GL.CD_ITEM,
		   MI.NM_ITEM,
		   GL.TP_ITEM,
		   MI.STND_ITEM,
		   GL.DT_DUEDATE,
		   ISNULL(QI.DT_IO, '') AS DT_IO,
		   GL.DT_REQGI,
		   GL.YN_INSPECT,
		   GL.CD_SL,
		   MS.NM_SL,
		   GL.TP_GI,
		   MJ.NM_QTIOTP AS NM_GI,
		   GL.CD_EXCH,
		   GL.QT_GIR_IM,
		   NEOE.FN_CZ_GET_STOCK_QTY (SL.CD_COMPANY, CONVERT(CHAR(8), GETDATE(), 112), SL.CD_PLANT, SL.CD_ITEM) AS QT_INV,
		   ISNULL(QI.QT_TAX, 0) AS QT_TAX,
		   ISNULL(QI.CUSTOMS, 0) AS CUSTOMS,
		   ISNULL(QI.QT_BL, 0) AS QT_BL,
		   GL.QT_GIR,
		   GL.QT_GIR_STOCK,
		   GL.QT_GI,
		   GL.UM,
		   GL.AM_GIR,
		   GL.RT_EXCH,
		   GL.AM_GIRAMT,
		   GL.AM_VAT,
		   (GL.AM_GIRAMT + GL.AM_VAT) AS AMT,
		   GL.UNIT,
		   GL.GI_PARTNER,
		   MP.LN_PARTNER,
		   GL.NO_PROJECT,
		   SP.NM_PROJECT,
		   GL.CD_SALEGRP,
		   MG.NM_SALEGRP,
		   GL.DC_RMK,
		   SL.NO_PO_PARTNER,
		   SL.NO_POLINE_PARTNER,
		   GL.RT_VAT,
		   GL.TP_VAT,
		   GL.NO_EMP,
		   GL.TP_IV,
		   GL.FG_TAXP,
		   GH.TP_BUSI,
		   GL.FG_LC_OPEN,
		   GL.GIR,
		   GL.IV,
		   GL.TP_UM_TAX,
		   GL.UMVAT_GIR,
		   MI.RT_PLUS,
		   SL.QT_SO,
		   MI.QT_WIDTH * MI.QT_LENGTH AS QT_SQUARE,
		   CASE WHEN (MI.QT_WIDTH * MI.QT_LENGTH) = 0 THEN 0 ELSE GL.UM / (MI.QT_WIDTH * MI.QT_LENGTH) END AS UM_SQUARE,
		   MI.UNIT_SO_FACT, 
		   GL.YN_ADD_STOCK,
		   SH.YN_CONFIRM,
		   MI.GRP_ITEM,
		   (CASE WHEN ISNULL(GL.QT_GIR_STOCK, 0) > 0 AND GL.QT_GIR = GL.QT_GIR_STOCK THEN ''
				 WHEN ISNULL(QI.DT_IO, '') <> '' THEN STUFF((SELECT DISTINCT ',' + A.CD_LOCATION
		   													 FROM MM_QTIO_LOCATION A
		   													 JOIN MM_QTIO B ON B.CD_COMPANY = A.CD_COMPANY AND B.NO_IO = A.NO_IO AND B.NO_IOLINE = A.NO_IOLINE
		   													 WHERE B.FG_PS = '1'
		   													 AND B.CD_COMPANY = GL.CD_COMPANY
		   													 AND B.CD_PJT = GL.NO_SO
		   													 AND B.NO_PSOLINE_MGMT = GL.SEQ_SO
		   													 FOR XML PATH('')),1,1,'')
		   									   ELSE '미입고' END) AS NO_LOCATION,
		   (CASE WHEN ISNULL(GL.QT_GIR_STOCK, 0) > 0 THEN MI.CD_ZONE ELSE '' END) AS NO_LOCATION_STOCK,
		   (CASE WHEN ISNULL(GL.QT_GIR_STOCK, 0) > 0 AND GL.QT_GIR = GL.QT_GIR_STOCK THEN 'STOCK'
																					 ELSE STUFF((SELECT DISTINCT ',' + PH.CD_PARTNER
																								 FROM PU_POL PL
																								 LEFT JOIN PU_POH PH ON PH.CD_COMPANY = PL.CD_COMPANY AND PH.NO_PO = PL.NO_PO
																								 WHERE PL.CD_COMPANY = GL.CD_COMPANY
																								 AND PL.NO_SO =GL.NO_SO
																								 AND PL.NO_SOLINE = GL.SEQ_SO
																								 FOR XML PATH('')),1,1,'') END) AS CD_SUPPLIER,
		   (CASE WHEN ISNULL(GL.QT_GIR_STOCK, 0) > 0 AND GL.QT_GIR = GL.QT_GIR_STOCK THEN 'STOCK'
																					 ELSE STUFF((SELECT DISTINCT ',' + MP.LN_PARTNER 
																								 FROM PU_POL PL
																								 LEFT JOIN PU_POH PH ON PH.CD_COMPANY = PL.CD_COMPANY AND PH.NO_PO = PL.NO_PO
																								 LEFT JOIN MA_PARTNER MP ON MP.CD_COMPANY = PH.CD_COMPANY AND MP.CD_PARTNER = PH.CD_PARTNER
																								 WHERE PL.CD_COMPANY = GL.CD_COMPANY
																								 AND PL.NO_SO =GL.NO_SO
																								 AND PL.NO_SOLINE = GL.SEQ_SO
																								 FOR XML PATH('')),1,1,'') END) AS NM_SUPPLIER
	FROM CZ_SA_GIRL_LOG GL               
	JOIN CZ_SA_GIRH_LOG GH ON GH.CD_COMPANY = GL.CD_COMPANY AND GH.NO_GIR = GL.NO_GIR AND GH.NO_HST = GL.NO_HST      
	LEFT JOIN MA_PITEM MI ON MI.CD_COMPANY = GL.CD_COMPANY AND MI.CD_ITEM = GL.CD_ITEM AND MI.CD_PLANT = GH.CD_PLANT 
	LEFT JOIN CZ_SA_QTNL QL ON QL.CD_COMPANY = GL.CD_COMPANY AND QL.NO_FILE = GL.NO_SO AND QL.NO_LINE = GL.SEQ_SO
	LEFT JOIN SA_SOL SL ON SL.CD_COMPANY = GL.CD_COMPANY AND SL.NO_SO = GL.NO_SO AND SL.SEQ_SO = GL.SEQ_SO
	LEFT JOIN SA_SOH SH ON SH.CD_COMPANY = SL.CD_COMPANY AND SH.NO_SO = SL.NO_SO
	LEFT JOIN MA_SL MS ON MS.CD_COMPANY = GL.CD_COMPANY AND MS.CD_SL = GL.CD_SL AND MS.CD_PLANT = GH.CD_PLANT            
	LEFT JOIN MM_EJTP MJ ON MJ.CD_COMPANY = GL.CD_COMPANY AND MJ.CD_QTIOTP = GL.TP_GI             
	LEFT JOIN MA_PARTNER MP ON MP.CD_COMPANY = GL.CD_COMPANY AND MP.CD_PARTNER = GL.GI_PARTNER
	LEFT JOIN SA_PROJECTH SP ON SP.CD_COMPANY = GL.CD_COMPANY AND SP.NO_PROJECT = GL.NO_PROJECT       
	LEFT JOIN MA_SALEGRP MG ON MG.CD_COMPANY = GL.CD_COMPANY AND MG.CD_SALEGRP = GL.CD_SALEGRP    
	LEFT JOIN CZ_TR_INVL TI ON TI.CD_COMPANY = GL.CD_COMPANY AND TI.NO_INV = GL.NO_INV AND TI.NO_LINE = GL.SEQ_GIR
	LEFT JOIN (SELECT PL.CD_COMPANY, PL.NO_SO, PL.NO_SOLINE,
			   	      MAX(IL.DT_IO) AS DT_IO,
			   		  SUM(TX.QT_TAX) AS QT_TAX,
					  SUM(TX.CUSTOMS) AS CUSTOMS,
					  SUM(CASE WHEN ISNULL(PL.YN_BL, 'N') = 'Y' THEN PL.QT_PO ELSE 0 END) AS QT_BL
			   FROM PU_POL PL
			   LEFT JOIN (SELECT IL.CD_COMPANY, IL.NO_PSO_MGMT, IL.NO_PSOLINE_MGMT,
			   			  	     MAX(IH.DT_IO) AS DT_IO 
			   			  FROM MM_QTIO IL
			   			  LEFT JOIN MM_QTIOH IH ON IH.CD_COMPANY = IL.CD_COMPANY AND IH.NO_IO = IL.NO_IO
			   			  GROUP BY IL.CD_COMPANY, IL.NO_PSO_MGMT, IL.NO_PSOLINE_MGMT) IL
			   ON IL.CD_COMPANY = PL.CD_COMPANY AND IL.NO_PSO_MGMT = PL.NO_PO AND IL.NO_PSOLINE_MGMT = PL.NO_LINE
			   LEFT JOIN (SELECT IL.CD_COMPANY, IL.NO_PO, IL.NO_POLINE,
			   		   			 SUM(IL.QT_TAX) AS QT_TAX,
								 SUM(DL.CUSTOMS) AS CUSTOMS
			   			  FROM CZ_FI_IMP_PMTL IL
						  LEFT JOIN CZ_PU_IMPORT_DECLARATIONL DL ON DL.CD_COMPANY = IL.CD_COMPANY AND REPLACE(DL.NO_IMPORT, '-', '') = IL.NO_IMPORT AND DL.SEQ = IL.SEQ_IMPORT
			   			  GROUP BY IL.CD_COMPANY, IL.NO_PO, IL.NO_POLINE) TX
			   ON TX.CD_COMPANY = PL.CD_COMPANY AND TX.NO_PO = PL.NO_PO AND TX.NO_POLINE = PL.NO_LINE
	GROUP BY PL.CD_COMPANY, PL.NO_SO, PL.NO_SOLINE) QI
	ON QI.CD_COMPANY = SL.CD_COMPANY AND QI.NO_SO = SL.NO_SO AND QI.NO_SOLINE = SL.SEQ_SO
	WHERE GL.CD_COMPANY = @P_CD_COMPANY
	AND GL.TP_GIR = '001'          
	AND GL.NO_GIR = @P_NO_GIR
	AND GL.NO_HST = @P_NO_HST  
	ORDER BY GL.NO_GIR, GL.SEQ_GIR
END

