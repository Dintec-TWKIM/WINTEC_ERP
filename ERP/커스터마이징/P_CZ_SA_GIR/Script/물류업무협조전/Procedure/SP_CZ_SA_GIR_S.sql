		   GL.NO_PROJECT,
		   SP.NM_PROJECT,
		   GL.SEQ_PROJECT,
		   GL.CD_SALEGRP,
		   MG.NM_SALEGRP,
		   GL.DC_RMK,
		   SL.NO_PO_PARTNER,
		   SL.NO_POLINE_PARTNER,
		   GL.RT_VAT,
		   GL.TP_VAT,
		   GL.NO_EMP,
		   GL.TP_IV,
		   GL.FG_TAXP,
