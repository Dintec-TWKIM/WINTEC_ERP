ALTER PROCEDURE [NEOE].[SP_CZ_SA_GIR_AUTO_CPR_S]
(
    @P_CD_COMPANY    NVARCHAR(7),
    @P_NO_GIR        NVARCHAR(20)
)
AS

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT MD.NO_EMAIL,
       WD.NO_DELIVERY_EMAIL,
       WD.YN_EXCLUDE_CPR,
       WD.DC_RMK_CI,
       WD.DC_RMK_CPR,
       WD.YN_CPR
FROM CZ_SA_GIRH_WORK_DETAIL WD
LEFT JOIN CZ_MA_DELIVERY MD ON MD.CD_COMPANY = WD.CD_COMPANY AND MD.CD_PARTNER = WD.CD_DELIVERY_TO
WHERE WD.CD_COMPANY = @P_CD_COMPANY
AND WD.NO_GIR = @P_NO_GIR

GO