ALTER PROCEDURE [NEOE].[SP_CZ_SA_EWS_EXCEPT_PARTNER_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_SA_EWS_EXCEPT_PARTNER A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY		NVARCHAR(7),
			  IDX_HST			INT) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.IDX_HST = B.IDX_HST
-- ================================================== INSERT
INSERT INTO CZ_SA_EWS_EXCEPT_PARTNER
(
	CD_COMPANY,
	CD_PARTNER,
	DT_EXPIRE,
	AM_LIMIT,
	YN_PAY,
	DC_RMK,
	ID_INSERT,
	DTS_INSERT
)
SELECT CD_COMPANY,
	   CD_PARTNER,
	   DT_EXPIRE,
	   AM_LIMIT,
	   YN_PAY,
	   DC_RMK,
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
  FROM OPENXML (@DOC, '/XML/I', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
				CD_PARTNER	    NVARCHAR(20),
			    DT_EXPIRE	    NVARCHAR(8),
				AM_LIMIT		NUMERIC(18, 4),
				YN_PAY			NVARCHAR(1),
				DC_RMK			NVARCHAR(MAX)) 
-- ================================================== UPDATE
UPDATE A 
   SET A.DT_EXPIRE = B.DT_EXPIRE,
	   A.AM_LIMIT = B.AM_LIMIT,
	   A.YN_PAY = B.YN_PAY,
	   A.DC_RMK	= B.DC_RMK,
	   A.ID_UPDATE = @P_ID_USER,
	   A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
  FROM CZ_SA_EWS_EXCEPT_PARTNER A 
  JOIN OPENXML (@DOC, '/XML/U', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
				IDX_HST			INT,
				DT_EXPIRE		NVARCHAR(8),
				AM_LIMIT		NUMERIC(18, 4),
				YN_PAY			NVARCHAR(1),
				DC_RMK			NVARCHAR(MAX)) B 
  ON A.CD_COMPANY = B.CD_COMPANY
  AND A.IDX_HST = B.IDX_HST

EXEC SP_XML_REMOVEDOCUMENT @DOC 



GO