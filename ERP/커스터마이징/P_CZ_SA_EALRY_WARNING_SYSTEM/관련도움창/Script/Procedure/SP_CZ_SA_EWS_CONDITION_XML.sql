ALTER PROCEDURE [NEOE].[SP_CZ_SA_EWS_CONDITION_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_SA_EWS_CONDITION A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY		NVARCHAR(7),
			  YN_SIMULATION		NVARCHAR(1),
			  TP_GUBUN			NVARCHAR(1),
			  CD_EWS			NVARCHAR(10)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.YN_SIMULATION = B.YN_SIMULATION
AND A.TP_GUBUN = B.TP_GUBUN
AND A.CD_EWS = B.CD_EWS
-- ================================================== INSERT
INSERT INTO CZ_SA_EWS_CONDITION
(
	CD_COMPANY,
	YN_SIMULATION,
	TP_GUBUN,
	CD_EWS,
	DC_EWS,
	DC_EWS1,
	DC_EWS2,
	DC_EWS3,
	YN_USE,
	ID_INSERT,
	DTS_INSERT
)
SELECT CD_COMPANY,
	   YN_SIMULATION,
	   TP_GUBUN,
	   CD_EWS,
	   DC_EWS,
	   DC_EWS1,
	   DC_EWS2,
	   DC_EWS3,
	   YN_USE,
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
  FROM OPENXML (@DOC, '/XML/I', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
				YN_SIMULATION	NVARCHAR(1),
			    TP_GUBUN		NVARCHAR(1),
			    CD_EWS			NVARCHAR(10),
				DC_EWS			NVARCHAR(200),
				DC_EWS1			NVARCHAR(200),
				DC_EWS2			NVARCHAR(50),
				DC_EWS3			NVARCHAR(50),
				YN_USE			NVARCHAR(1)) 
-- ================================================== UPDATE
UPDATE A 
   SET A.DC_EWS = B.DC_EWS,
	   A.DC_EWS1 = B.DC_EWS1,
	   A.DC_EWS2 = B.DC_EWS2,
	   A.DC_EWS3 = B.DC_EWS3,
	   A.YN_USE = B.YN_USE,
	   A.ID_UPDATE = @P_ID_USER,
	   A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
  FROM CZ_SA_EWS_CONDITION A 
  JOIN OPENXML (@DOC, '/XML/U', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
				YN_SIMULATION	NVARCHAR(1),
			    TP_GUBUN		NVARCHAR(1),
			    CD_EWS			NVARCHAR(10),
				DC_EWS			NVARCHAR(200),
				DC_EWS1			NVARCHAR(200),
				DC_EWS2			NVARCHAR(50),
				DC_EWS3			NVARCHAR(50),
				YN_USE			NVARCHAR(1)) B 
  ON A.CD_COMPANY = B.CD_COMPANY
  AND A.YN_SIMULATION = B.YN_SIMULATION
  AND A.TP_GUBUN = B.TP_GUBUN
  AND A.CD_EWS = B.CD_EWS

EXEC SP_XML_REMOVEDOCUMENT @DOC 



GO