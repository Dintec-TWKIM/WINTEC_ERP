ALTER PROCEDURE [NEOE].[SP_CZ_SA_EWS_LOG_S]
(
	@P_CD_COMPANY		NVARCHAR(7),
	@P_CD_PARTNER		NVARCHAR(20)
) 
AS
 
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT EL.DT_EWS,
	   (CASE EL.CD_LEVEL WHEN '0' THEN '정상'
						 WHEN '1' THEN '주의요망'
						 WHEN '2' THEN '사용불가' END) AS NM_LEVEL,
	   EL.NM_RESULT1,
	   EL.NM_RESULT2,
	   EL.NM_RESULT3,
	   EL.NM_RESULT4,
	   EL.NM_RESULT5,
	   EL.NM_RESULT6,
	   EL.NM_RESULT7
FROM CZ_SA_EWS_LOG EL
WHERE EL.CD_COMPANY = @P_CD_COMPANY
AND EL.CD_PARTNER = @P_CD_PARTNER
ORDER BY EL.DT_EWS DESC

GO