ALTER PROCEDURE [NEOE].[SP_CZ_MA_HULL_ENGINE_ITEM_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_MA_HULL_ENGINE_ITEM A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (NO_IMO		NVARCHAR(10),
			  NO_ENGINE 	INT,
			  NO_PLATE 		NVARCHAR(100)) B 
ON A.NO_IMO = B.NO_IMO 
AND A.NO_ENGINE = B.NO_ENGINE
AND A.NO_PLATE = B.NO_PLATE
-- ================================================== INSERT
INSERT INTO CZ_MA_HULL_ENGINE_ITEM 
(
	NO_IMO,
	NO_ENGINE,
	NO_PLATE,
	NM_PLATE,
	WEIGHT,
	UNIT,
	CD_ITEM,
	UCODE,
	NO_POSITION,
	QT_PLATE,
	DC_RMK,
	YN_UPLOAD,
	KCODE,
	KCODE2,
	ID_INSERT,
	DTS_INSERT
)
SELECT NO_IMO,
	   NO_ENGINE,
	   NO_PLATE,
	   NM_PLATE,
	   WEIGHT,
	   UNIT,
	   CD_ITEM,
	   UCODE,
	   NO_POSITION,
	   QT_PLATE,
	   REPLACE(DC_RMK, CHAR(10), CHAR(13) + CHAR(10)),
	   'N' AS YN_UPLOAD,
	   KCODE,
	   KCODE2,
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
  FROM OPENXML (@DOC, '/XML/I', 2) 
          WITH (NO_IMO			NVARCHAR(10), 
				NO_ENGINE		INT,
				NO_PLATE		NVARCHAR(100),
				NM_PLATE		NVARCHAR(200),
				WEIGHT			NUMERIC(11, 3),
				UNIT			NVARCHAR(4),
				CD_ITEM			NVARCHAR(20),
				UCODE			NVARCHAR(20),
				NO_POSITION		NUMERIC(11, 0),
				QT_PLATE		NUMERIC(11, 0),
				DC_RMK			NVARCHAR(1000),
				KCODE			NVARCHAR(20),
				KCODE2			NVARCHAR(20)) 
-- ================================================== UPDATE    
UPDATE A 
   SET A.NM_PLATE = B.NM_PLATE,
	   A.WEIGHT = B.WEIGHT,
	   A.UNIT = B.UNIT,
	   A.CD_ITEM = B.CD_ITEM,
	   A.UCODE = B.UCODE,
	   A.NO_POSITION = B.NO_POSITION,
	   A.QT_PLATE = B.QT_PLATE,
	   A.DC_RMK = REPLACE(B.DC_RMK, CHAR(10), CHAR(13) + CHAR(10)),
	   A.KCODE = B.KCODE,
	   A.KCODE2 = B.KCODE2,
	   A.ID_UPDATE = @P_ID_USER, 
	   A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
  FROM CZ_MA_HULL_ENGINE_ITEM A 
  JOIN OPENXML (@DOC, '/XML/U', 2) 
          WITH (NO_IMO			NVARCHAR(10), 
				NO_ENGINE		INT,
				NO_PLATE		NVARCHAR(100),
				NM_PLATE		NVARCHAR(200),
				WEIGHT			NUMERIC(11, 3),
				UNIT			NVARCHAR(4),
				CD_ITEM			NVARCHAR(20),
				UCODE			NVARCHAR(20),
				NO_POSITION		NUMERIC(11, 0),
				QT_PLATE		NUMERIC(11, 0),
				DC_RMK			NVARCHAR(1000),
				KCODE			NVARCHAR(20),
				KCODE2			NVARCHAR(20)) B 
  ON A.NO_IMO = B.NO_IMO 
  AND A.NO_ENGINE = B.NO_ENGINE
  AND A.NO_PLATE = B.NO_PLATE

EXEC SP_XML_REMOVEDOCUMENT @DOC 

GO

