ALTER PROCEDURE [NEOE].[SP_CZ_MA_HULL_VENDOR_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_MA_HULL_VENDOR A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (NO_IMO		NVARCHAR(10),
			  CD_VENDOR 	NVARCHAR(20)) B 
ON A.NO_IMO = B.NO_IMO
AND A.CD_VENDOR = B.CD_VENDOR
-- ================================================== INSERT
INSERT INTO CZ_MA_HULL_VENDOR 
(
	NO_IMO,
	CD_VENDOR,
	ID_INSERT,
	DTS_INSERT
)
SELECT NO_IMO,
	   CD_VENDOR,
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
  FROM OPENXML (@DOC, '/XML/I', 2) 
          WITH (NO_IMO			NVARCHAR(10), 
				CD_VENDOR		NVARCHAR(20)) 

EXEC SP_XML_REMOVEDOCUMENT @DOC 

GO

