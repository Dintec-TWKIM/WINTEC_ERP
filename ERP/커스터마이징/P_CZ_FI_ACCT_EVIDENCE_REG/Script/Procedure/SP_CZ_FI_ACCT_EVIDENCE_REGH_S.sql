ALTER PROCEDURE [NEOE].[SP_CZ_FI_ACCT_EVIDENCE_REGH_S] 
(
	@P_CD_COMPANY		NVARCHAR(7)
) 
AS

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT EH.CD_ACCT,
	   AC.NM_ACCT 
FROM CZ_FI_ACCT_EVIDENCEH EH
LEFT JOIN FI_ACCTCODE AC ON AC.CD_COMPANY = EH.CD_COMPANY AND AC.CD_ACCT = EH.CD_ACCT
WHERE EH.CD_COMPANY = @P_CD_COMPANY


GO

