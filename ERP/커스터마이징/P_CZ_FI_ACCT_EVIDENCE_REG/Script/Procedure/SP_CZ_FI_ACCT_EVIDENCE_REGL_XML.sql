ALTER PROCEDURE [NEOE].[SP_CZ_FI_ACCT_EVIDENCE_REGL_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
 
EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_FI_ACCT_EVIDENCEL A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY NVARCHAR(7),
			  CD_ACCT	 NVARCHAR(10),
			  NO_LINE	 NUMERIC(18, 0)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.CD_ACCT = B.CD_ACCT
AND A.NO_LINE = B.NO_LINE
-- ================================================== INSERT
INSERT INTO CZ_FI_ACCT_EVIDENCEL 
(
	CD_COMPANY,
	CD_ACCT,
	NO_LINE,
	FG_TAX,
	TP_EVIDENCE,			 
	ID_INSERT,
	DTS_INSERT
)
SELECT CD_COMPANY,
	   CD_ACCT,
	   NO_LINE,
	   FG_TAX,
	   TP_EVIDENCE,		
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
FROM OPENXML (@DOC, '/XML/I', 2) 
        WITH (CD_COMPANY	 NVARCHAR(7),
			  CD_ACCT		 NVARCHAR(10),
			  NO_LINE		 NUMERIC(18, 0),
			  FG_TAX		 NVARCHAR(3),
			  TP_EVIDENCE	 NVARCHAR(3)) 
-- ================================================== UPDATE    
UPDATE A 
   SET A.FG_TAX = B.FG_TAX,
	   A.TP_EVIDENCE = B.TP_EVIDENCE,
	   A.ID_UPDATE = @P_ID_USER, 
	   A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
  FROM CZ_FI_ACCT_EVIDENCEL A 
  JOIN OPENXML (@DOC, '/XML/U', 2) 
          WITH (CD_COMPANY	 NVARCHAR(7),
			    CD_ACCT		 NVARCHAR(10),
			    NO_LINE		 NUMERIC(18, 0),
			    FG_TAX		 NVARCHAR(3),
			    TP_EVIDENCE	 NVARCHAR(3)) B 
  ON A.CD_COMPANY = B.CD_COMPANY
  AND A.CD_ACCT = B.CD_ACCT
  AND A.NO_LINE = B.NO_LINE

EXEC SP_XML_REMOVEDOCUMENT @DOC 

GO

