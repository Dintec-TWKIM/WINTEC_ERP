ALTER PROCEDURE [NEOE].[SP_CZ_FI_PARTNERPTR_S]
(
	@P_CD_COMPANY  	NVARCHAR(7),
	@P_CD_PARTNER  	NVARCHAR(20)
) 
AS

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT FP.CD_COMPANY,
	   FP.CD_PARTNER,
	   FP.SEQ,
	   FP.USE_YN,
	   FP.TP_PTR,
	   FP.YN_TYPE,
	   FP.YN_OUTSTANDING_INV,
	   FP.NM_PTR,
	   FP.EN_PTR,
	   FP.NM_DEPT,
	   FP.NM_DUTY_RESP,
	   FP.EN_DUTY_RESP,
	   FP.NM_EMAIL,
	   FP.NO_HP,
	   FP.NO_TEL,
	   FP.NO_FAX,
	   FP.CLIENT_NOTE,
	   FP.DC_PHOTO,
	   FP.YN_LIMIT,
	   FP.NO_MOBILE,
	   FP.ID_INSERT,
	   MU.NM_USER AS NM_INSERT,
	   FP.DTS_INSERT,
	   FP.ID_UPDATE,
	   MU1.NM_USER AS NM_UPDATE,
	   FP.DTS_UPDATE
FROM FI_PARTNERPTR FP
LEFT JOIN MA_USER MU ON MU.CD_COMPANY = FP.CD_COMPANY AND MU.ID_USER = FP.ID_INSERT
LEFT JOIN MA_USER MU1 ON MU1.CD_COMPANY = FP.CD_COMPANY AND MU1.ID_USER = FP.ID_UPDATE
WHERE FP.CD_COMPANY = @P_CD_COMPANY
AND FP.CD_PARTNER = @P_CD_PARTNER

GO

