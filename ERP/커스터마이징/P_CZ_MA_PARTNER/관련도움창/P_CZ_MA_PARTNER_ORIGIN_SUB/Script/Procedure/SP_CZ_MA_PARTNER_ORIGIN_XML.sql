ALTER PROCEDURE [NEOE].[SP_CZ_MA_PARTNER_ORIGIN_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_MA_PARTNER_ORIGIN A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY NVARCHAR(7),
		      CD_PARTNER NVARCHAR(20),
			  CD_ORIGIN	 NVARCHAR(20)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.CD_PARTNER = B.CD_PARTNER
AND A.CD_ORIGIN = B.CD_ORIGIN
-- ================================================== INSERT
INSERT INTO CZ_MA_PARTNER_ORIGIN 
(
	CD_COMPANY,
	CD_PARTNER,
	CD_ORIGIN,
	NM_ORIGIN,
	NM_ORIGIN_RPT,
	ID_INSERT,
	DTS_INSERT
)
SELECT CD_COMPANY,
	   CD_PARTNER,
	   CD_ORIGIN,
	   NM_ORIGIN,
	   NM_ORIGIN_RPT,
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
  FROM OPENXML (@DOC, '/XML/I', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
		        CD_PARTNER		NVARCHAR(20),
			    CD_ORIGIN		NVARCHAR(20),
			    NM_ORIGIN		NVARCHAR(200),
				NM_ORIGIN_RPT	NVARCHAR(100)) 
-- ================================================== UPDATE

UPDATE A 
   SET A.NM_ORIGIN = B.NM_ORIGIN,
	   A.NM_ORIGIN_RPT = B.NM_ORIGIN_RPT,
	   A.ID_UPDATE = @P_ID_USER,
	   A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
  FROM CZ_MA_PARTNER_ORIGIN A 
  JOIN OPENXML (@DOC, '/XML/U', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
		        CD_PARTNER		NVARCHAR(20),
			    CD_ORIGIN		NVARCHAR(20),
			    NM_ORIGIN		NVARCHAR(200),
			    NM_ORIGIN_RPT	NVARCHAR(50)) B 
  ON A.CD_COMPANY = B.CD_COMPANY
  AND A.CD_PARTNER = B.CD_PARTNER
  AND A.CD_ORIGIN = B.CD_ORIGIN 

EXEC SP_XML_REMOVEDOCUMENT @DOC 



GO

