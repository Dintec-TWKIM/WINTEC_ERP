ALTER PROCEDURE [NEOE].[SP_CZ_SA_COMMISSION_MNGL_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_SA_COMMISSIONL A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY	NVARCHAR(7),
			  NO_COMMISSION NVARCHAR(20),
			  SEQ			NUMERIC(5, 0)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.NO_COMMISSION = B.NO_COMMISSION
AND A.SEQ = B.SEQ
-- ================================================== INSERT
INSERT INTO CZ_SA_COMMISSIONL 
(
	CD_COMPANY,
	NO_COMMISSION,
	SEQ,
	NM_PTR,
	NM_EMAIL,
	NM_DEPT,
	NM_DUTY_RESP,
	DC_RMK,
	ID_INSERT,
	DTS_INSERT
)
SELECT CD_COMPANY,
	   NO_COMMISSION,
	   SEQ,
	   NM_PTR,
	   NM_EMAIL,
	   NM_DEPT,
	   NM_DUTY_RESP,
	   REPLACE(DC_RMK, CHAR(10), CHAR(13) + CHAR(10)),
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
  FROM OPENXML (@DOC, '/XML/I', 2) 
          WITH (CD_COMPANY	  NVARCHAR(7),
			    NO_COMMISSION NVARCHAR(20),
				SEQ			  NUMERIC(5, 0),
				NM_PTR		  NVARCHAR(30),
				NM_EMAIL	  NVARCHAR(80),
				NM_DEPT		  NVARCHAR(100),
				NM_DUTY_RESP  NVARCHAR(100),
				DC_RMK		  NVARCHAR(MAX)) 
-- ================================================== UPDATE    
UPDATE A 
SET A.DC_RMK = REPLACE(B.DC_RMK, CHAR(10), CHAR(13) + CHAR(10)),
    A.ID_UPDATE = @P_ID_USER, 
    A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
FROM CZ_SA_COMMISSIONL A 
JOIN OPENXML (@DOC, '/XML/U', 2) 
        WITH (CD_COMPANY	  NVARCHAR(7),
			  NO_COMMISSION	  NVARCHAR(20),
			  SEQ			  NUMERIC(5, 0),
			  DC_RMK		  NVARCHAR(MAX)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.NO_COMMISSION = B.NO_COMMISSION
AND A.SEQ = B.SEQ

EXEC SP_XML_REMOVEDOCUMENT @DOC 



GO

