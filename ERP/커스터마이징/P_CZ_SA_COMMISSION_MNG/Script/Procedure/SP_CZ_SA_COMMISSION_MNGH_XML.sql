ALTER PROCEDURE [NEOE].[SP_CZ_SA_COMMISSION_MNGH_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_SA_COMMISSIONH A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY	NVARCHAR(7),
			  NO_COMMISSION NVARCHAR(20)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.NO_COMMISSION = B.NO_COMMISSION

DELETE A 
FROM CZ_SA_COMMISSIONL A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY	NVARCHAR(7),
			  NO_COMMISSION NVARCHAR(20)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.NO_COMMISSION = B.NO_COMMISSION

DELETE A 
FROM CZ_SA_COMMISSIOND A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY	NVARCHAR(7),
			  NO_COMMISSION NVARCHAR(20)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.NO_COMMISSION = B.NO_COMMISSION
-- ================================================== INSERT
INSERT INTO CZ_SA_COMMISSIONH 
(
	CD_COMPANY,
	NO_COMMISSION,
	CD_PARTNER,
	DC_COMMISSION,
	YN_ADDED,
	YN_CHARGE,
	TP_DATE,
	DT_START,
	DT_END,
	RT_COMMISSION,
	AM_COMMISSION,
	ID_INSERT,
	DTS_INSERT
)
SELECT CD_COMPANY,
	   NO_COMMISSION,
	   CD_PARTNER,
	   REPLACE(DC_COMMISSION, CHAR(10), CHAR(13) + CHAR(10)),
	   YN_ADDED,
	   YN_CHARGE,
	   TP_DATE,
	   DT_START,
	   DT_END,
	   RT_COMMISSION,
	   AM_COMMISSION,
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
  FROM OPENXML (@DOC, '/XML/I', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
			    NO_COMMISSION	NVARCHAR(20),
				CD_PARTNER		NVARCHAR(20),
				DC_COMMISSION	NVARCHAR(MAX),
				YN_ADDED		NVARCHAR(1),
				YN_CHARGE		NVARCHAR(1),
				TP_DATE			NVARCHAR(4),
				DT_START		NVARCHAR(8),
				DT_END			NVARCHAR(8),
				RT_COMMISSION	NUMERIC(5, 2),
				AM_COMMISSION	NUMERIC(18, 2)) 
-- ================================================== UPDATE    
UPDATE A 
   SET A.CD_PARTNER = B.CD_PARTNER,
	   A.DC_COMMISSION = REPLACE(B.DC_COMMISSION, CHAR(10), CHAR(13) + CHAR(10)),
	   A.YN_ADDED = B.YN_ADDED,
	   A.YN_CHARGE = B.YN_CHARGE,
	   A.TP_DATE = B.TP_DATE,
	   A.DT_START = B.DT_START,
	   A.DT_END = B.DT_END,
	   A.RT_COMMISSION = B.RT_COMMISSION,
	   A.AM_COMMISSION = B.AM_COMMISSION,
	   A.ID_UPDATE = @P_ID_USER, 
	   A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
  FROM CZ_SA_COMMISSIONH A 
  JOIN OPENXML (@DOC, '/XML/U', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
			    NO_COMMISSION	NVARCHAR(20),
				CD_PARTNER		NVARCHAR(20),
				DC_COMMISSION	NVARCHAR(MAX),
				YN_ADDED		NVARCHAR(1),
				YN_CHARGE		NVARCHAR(1),
				TP_DATE			NVARCHAR(4),
				DT_START		NVARCHAR(8),
				DT_END			NVARCHAR(8),
				RT_COMMISSION	NUMERIC(5, 2),
				AM_COMMISSION	NUMERIC(18, 2)) B 
  ON A.CD_COMPANY = B.CD_COMPANY
  AND A.NO_COMMISSION = B.NO_COMMISSION

EXEC SP_XML_REMOVEDOCUMENT @DOC 



GO

