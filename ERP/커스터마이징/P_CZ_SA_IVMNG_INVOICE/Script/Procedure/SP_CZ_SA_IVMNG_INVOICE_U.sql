ALTER PROCEDURE [NEOE].[SP_CZ_SA_IVMNG_INVOICE_U]
(
	@P_CD_COMPANY	NVARCHAR(7),
	@P_NO_INVOICE	NVARCHAR(20),
	@P_DC_COMPANY	NVARCHAR(MAX),
	@P_DC_ADDRESS	NVARCHAR(MAX),
	@P_DC_TEL		NVARCHAR(MAX),
	@P_CD_NATION	NVARCHAR(10),
	@P_NO_IV		NVARCHAR(20),
	@P_NO_IO		NVARCHAR(20),
	@P_NM_CITY		NVARCHAR(100),
	@P_CD_POSTAL	NVARCHAR(100),
	@P_ID_UPDATE	NVARCHAR(15)
)
AS

UPDATE IV
SET IV.DC_COMPANY = @P_DC_COMPANY,
    IV.DC_ADDRESS = @P_DC_ADDRESS,
	IV.DC_TEL = @P_DC_TEL,
	IV.CD_NATION = @P_CD_NATION,
	IV.NO_IV = @P_NO_IV,
	IV.NO_IO = @P_NO_IO,
	IV.NM_CITY = @P_NM_CITY,
	IV.CD_POSTAL = @P_CD_POSTAL,
	IV.ID_UPDATE = @P_ID_UPDATE,
	IV.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
FROM CZ_SA_IVMNG_INVOICE IV
WHERE IV.CD_COMPANY = @P_CD_COMPANY
AND IV.NO_INVOICE = @P_NO_INVOICE

GO

