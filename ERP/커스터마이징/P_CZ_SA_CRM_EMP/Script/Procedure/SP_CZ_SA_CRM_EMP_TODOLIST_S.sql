ALTER PROCEDURE [BX].[SP_CZ_SA_CRM_EMP_TODOLIST_S]
(
	@P_NO_EMP			NVARCHAR(20)
) 
AS
 
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT TD.TODO_ID,
	   TD.CATEGORY_ID,
	   TD.ORDERBY,
	   TD.TITLE,
	   TD.CONTENTS,
	   TD.PROC_CONTENTS,
	   TD.START_DT,
	   TD.END_DT,
	   TD.PROC_RATE,
	   TD.END_YN,
	   TD.CREATED_DT,
	   US.LOGON_CD AS ID_INSERT,
	   PD.CD_PARTNER,
	   MP.LN_PARTNER
FROM BX.TSOG_TODOLIST TD
JOIN BX.TCMG_USER US on US.USER_ID = TD.CREATED_BY
LEFT JOIN NEOE.NEOE.CZ_CRM_PARTNER_TODO PD ON PD.NO_TODO = TD.TODO_ID
LEFT JOIN NEOE.NEOE.MA_PARTNER MP ON MP.CD_COMPANY = PD.CD_COMPANY AND MP.CD_PARTNER = PD.CD_PARTNER
WHERE TD.DEL_YN = 0
AND US.LOGON_CD = @P_NO_EMP
ORDER BY TD.CREATED_DT DESC

GO

