ALTER PROCEDURE [NEOE].[SP_CZ_SA_SOSCH2H_U]  
(  
    @P_CD_COMPANY		NVARCHAR(7),
	@P_NO_SO			NVARCHAR(20),
	@P_DC_RMK_TEXT2		NVARCHAR(MAX),
	@P_DC_RMK1			NVARCHAR(MAX),
	@P_TXT_USERDEF1		NVARCHAR(100),
	@P_ID_UPDATE		NVARCHAR(15)
)
AS

UPDATE SA_SOH
SET DC_RMK_TEXT2 = @P_DC_RMK_TEXT2,
    DC_RMK1 = @P_DC_RMK1,
	TXT_USERDEF1 = @P_TXT_USERDEF1,
