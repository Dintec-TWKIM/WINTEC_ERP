ALTER PROCEDURE [NEOE].[SP_CZ_FI_CARD_VERIFYL_04_S]
(
	@P_CARD_NO		VARCHAR(20),
	@P_APPR_NO		VARCHAR(20)
)
AS  

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT CARD_NO AS ACCT_NO,
	   APPR_NO AS ADMIN_NO,
	   BILL_YYYYMM,
	   USE_DATE,
	   SETT_DATE,
	   CHAIN_NM,
	   BRANCHTYPE,
	   BILL_AMT,
	   USE_AMT,
	   DC_AMT,
	   DEMAND_AMT
FROM [EBRANCH].[ebranch].[dbo].TCARD_BILL
WHERE CARD_NO = @P_CARD_NO
AND APPR_NO = @P_APPR_NO

GO