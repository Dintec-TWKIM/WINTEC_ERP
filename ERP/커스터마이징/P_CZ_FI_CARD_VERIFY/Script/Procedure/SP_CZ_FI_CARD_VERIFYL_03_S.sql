ALTER PROCEDURE [NEOE].[SP_CZ_FI_CARD_VERIFYL_03_S]
(
	@P_CARD_NO		VARCHAR(20),
	@P_APPR_NO		VARCHAR(20)
)
AS  

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT CARD_NO AS ACCT_NO,
	   APPR_NO AS ADMIN_NO,
	   APPR_DATE,
	   APPR_TIME,
	   CHAIN_NM,
	   APPR_AMT,
	   CURRENCY,
	   USE_AMT,
	   WON_AMT,
	   BRANCHTYPE
FROM [EBRANCH].[ebranch].[dbo].TCARD_APPR
WHERE CARD_NO = @P_CARD_NO
AND APPR_NO = @P_APPR_NO

GO