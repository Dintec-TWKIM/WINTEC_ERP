ALTER PROCEDURE [NEOE].[SP_CZ_SA_CRM_PARTNER_ACTIVITY_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_CRM_PARTNER_ACTIVITY A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY NVARCHAR(7),
			  CD_PARTNER NVARCHAR(20),
			  NO_INDEX	 NUMERIC(18, 0)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.CD_PARTNER = B.CD_PARTNER
AND A.NO_INDEX = B.NO_INDEX
-- ================================================== INSERT
INSERT INTO CZ_CRM_PARTNER_ACTIVITY 
(
	CD_COMPANY,
	CD_PARTNER,
	NO_INDEX,
	YN_TODO,
	NM_TITLE,
	DT_START,
	DT_END,
	NO_PIC,
	NM_PIC,
	NO_EMP,
	NM_EMP,
	DC_ACTIVITY,			 
	ID_INSERT,
	DTS_INSERT
)
SELECT CD_COMPANY,
	   CD_PARTNER,
	   NO_INDEX,
	   YN_TODO,
	   NM_TITLE,
	   DT_START,
	   DT_END,
	   NO_PIC,
	   NM_PIC,
	   NO_EMP,
	   NM_EMP,
	   REPLACE(DC_ACTIVITY, CHAR(10), CHAR(13) + CHAR(10)),
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
  FROM OPENXML (@DOC, '/XML/I', 2) 
          WITH (CD_COMPANY	NVARCHAR(7),
				CD_PARTNER	NVARCHAR(20),
				NO_INDEX	NUMERIC(18, 0),
				YN_TODO		NVARCHAR(1),
				NM_TITLE	NVARCHAR(500),
				DT_START	NVARCHAR(8),
				DT_END		NVARCHAR(8),
				NO_PIC		NVARCHAR(500),
				NM_PIC		NVARCHAR(500),
				NO_EMP		NVARCHAR(500),
				NM_EMP		NVARCHAR(500),
			    DC_ACTIVITY	NVARCHAR(MAX)) 
-- ================================================== UPDATE    
UPDATE A 
   SET A.NM_TITLE = B.NM_TITLE,
	   A.DT_START = B.DT_START,
	   A.DT_END = B.DT_END,
	   A.NO_PIC = B.NO_PIC,
	   A.NM_PIC = B.NM_PIC,
	   A.NO_EMP = B.NO_EMP,
	   A.NM_EMP = B.NM_EMP,
	   A.DC_ACTIVITY = REPLACE(B.DC_ACTIVITY, CHAR(10), CHAR(13) + CHAR(10)),
	   A.ID_UPDATE = @P_ID_USER, 
	   A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
  FROM CZ_CRM_PARTNER_ACTIVITY A 
  JOIN OPENXML (@DOC, '/XML/U', 2) 
          WITH (CD_COMPANY	NVARCHAR(7),
				CD_PARTNER	NVARCHAR(20),
				NO_INDEX	NUMERIC(18, 0),
				NM_TITLE	NVARCHAR(500),
				DT_START	NVARCHAR(8),
				DT_END		NVARCHAR(8),
				NO_PIC		NVARCHAR(500),
				NM_PIC		NVARCHAR(500),
				NO_EMP		NVARCHAR(500),
				NM_EMP		NVARCHAR(500),
			    DC_ACTIVITY	NVARCHAR(MAX)) B 
  ON A.CD_COMPANY = B.CD_COMPANY
  AND A.CD_PARTNER = B.CD_PARTNER
  AND A.NO_INDEX = B.NO_INDEX

EXEC SP_XML_REMOVEDOCUMENT @DOC 




GO

