ALTER PROCEDURE [NEOE].[SP_CZ_SA_CRM_PARTNER_PIC_S1]
(
	@P_CD_COMPANY		NVARCHAR(7),
	@P_CD_PARTNER		NVARCHAR(20)
) 
AS
 
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT MC.NM_SYSDEF AS NM_TP_PTR,
	   FP.NM_PTR,
	   FP.NM_DEPT,
	   FP.NM_DUTY_RESP,
	   FP.NM_EMAIL,
	   FP.NO_HP,
	   FP.NO_TEL,
	   FP.NO_FAX,
	   1 AS QT_PIC,
	   FP.CLIENT_NOTE
FROM FI_PARTNERPTR FP
LEFT JOIN MA_CODEDTL MC ON MC.CD_COMPANY = FP.CD_COMPANY AND MC.CD_FIELD = 'CZ_MA00011' AND MC.CD_SYSDEF = FP.TP_PTR
WHERE FP.CD_COMPANY = @P_CD_COMPANY
AND FP.CD_PARTNER = @P_CD_PARTNER

GO

