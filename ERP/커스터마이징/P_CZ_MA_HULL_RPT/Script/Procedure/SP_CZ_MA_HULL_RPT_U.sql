ALTER PROCEDURE [NEOE].[SP_CZ_MA_HULL_RPT_U] 
(
	@P_CD_COMPANY	NVARCHAR(7),
	@P_NO_IMO		NVARCHAR(10),
	@P_YN_NEWSHIP	NVARCHAR(1),
	@P_DC_RMK		NVARCHAR(MAX),
	@P_ID_UPDATE	NVARCHAR(15)
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

UPDATE CZ_MA_HULL
SET YN_NEWSHIP = @P_YN_NEWSHIP,
	ID_UPDATE = @P_ID_UPDATE,
	DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
WHERE NO_IMO = @P_NO_IMO

IF EXISTS (SELECT 1 
		   FROM CZ_MA_HULL_RPT
		   WHERE CD_COMPANY = @P_CD_COMPANY
		   AND NO_IMO = @P_NO_IMO)
BEGIN
	UPDATE CZ_MA_HULL_RPT
	SET DC_RMK = @P_DC_RMK,
		ID_UPDATE = @P_ID_UPDATE,
		DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
	WHERE CD_COMPANY = @P_CD_COMPANY
	AND NO_IMO = @P_NO_IMO
END
ELSE
BEGIN
	INSERT INTO CZ_MA_HULL_RPT
	(
		CD_COMPANY,
		NO_IMO,
		DC_RMK,
		ID_INSERT,
		DTS_INSERT
	)
	VALUES
	(
		@P_CD_COMPANY,
		@P_NO_IMO,
		@P_DC_RMK,
		@P_ID_UPDATE,
		NEOE.SF_SYSDATE(GETDATE())
	)
END


GO

