ALTER PROCEDURE [NEOE].[SP_CZ_SA_READY_STATUSL_S]
(
	@P_CD_COMPANY      NVARCHAR(7),
	@P_NO_SO		   NVARCHAR(20)
) 
AS

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT SH.CD_COMPANY,
	   SH.NO_SO,
	   QL.NO_DSP,
	   QL.CD_ITEM_PARTNER,
	   QL.NM_ITEM_PARTNER,
	   QL.UNIT,
	   SL.QT_INQ,
	   SL.QT_SO,
	   SL.QT_PO,
	   SL.QT_GR,
	   SL.QT_GIR,
	   SL.QT_GI,
	   SL.DT_DUEDATE,
	   SL.TP_STATUS,
	   MC.NM_SYSDEF_E AS NM_TP_STATUS
FROM SA_SOH SH
JOIN (SELECT SL.*,
			 (CASE WHEN SL.QT_SO = SL.QT_GI THEN '005'
	   		 	   WHEN SL.QT_SO = QT_GIR THEN '004'
	   		 	   WHEN SL.QT_SO = QT_GR THEN '003'
	   		 	   WHEN SL.QT_SO = QT_PO THEN '002'
	   		 	   ELSE '001' END) TP_STATUS 
FROM (SELECT SL.CD_COMPANY, 
	 		 SL.NO_SO, 
	 		 SL.SEQ_SO,
	 		 SL.DT_DUEDATE,
	 		 SUM(QL.QT) AS QT_INQ,
	 		 SUM(SL.QT_SO) AS QT_SO,
	 		 (SUM(SL.QT_PO) + SUM(ISNULL(SB.QT_STOCK, 0))) AS QT_PO,
	 		 SUM(CASE WHEN QL.TP_BOM = 'P' THEN ISNULL(IL.QT_GR, 0)
	 		 							   ELSE ISNULL(PL.QT_RCV, 0) + ISNULL(SB.QT_BOOK, 0) END) AS QT_GR,
	 		 SUM(SL.QT_GIR) AS QT_GIR,
	 		 SUM(SL.QT_GI) AS QT_GI 
	 FROM SA_SOL SL
	 LEFT JOIN CZ_SA_QTNL QL ON QL.CD_COMPANY = SL.CD_COMPANY AND QL.NO_FILE = SL.NO_SO AND QL.NO_LINE = SL.SEQ_SO
	 LEFT JOIN CZ_SA_STOCK_BOOK SB ON SB.CD_COMPANY = SL.CD_COMPANY AND SB.NO_FILE = SL.NO_SO AND SB.NO_LINE = SL.SEQ_SO
	 LEFT JOIN (SELECT PL.CD_COMPANY, PL.NO_SO, PL.NO_SOLINE,
	 				   SUM(PL.QT_PO) AS QT_PO,
	 				   SUM(PL.QT_RCV) AS QT_RCV
	 			FROM PU_POL PL
	 			GROUP BY PL.CD_COMPANY, PL.NO_SO, PL.NO_SOLINE) PL
	 ON PL.CD_COMPANY = SL.CD_COMPANY AND PL.NO_SO = SL.NO_SO AND PL.NO_SOLINE = SL.SEQ_SO
	 LEFT JOIN (SELECT IL.CD_COMPANY, IL.NO_PSO_MGMT, IL.NO_PSOLINE_MGMT, SUM(IL.QT_IO) AS QT_GR 
	 			FROM MM_QTIO IL
	 			WHERE IL.FG_PS = '1'
	 			GROUP BY IL.CD_COMPANY, IL.NO_PSO_MGMT, IL.NO_PSOLINE_MGMT) IL
	 ON IL.CD_COMPANY = SL.CD_COMPANY AND IL.NO_PSO_MGMT = SL.NO_SO AND IL.NO_PSOLINE_MGMT = SL.SEQ_SO
	 WHERE SL.CD_ITEM NOT LIKE 'SD%'
	 GROUP BY SL.CD_COMPANY, SL.NO_SO, SL.SEQ_SO, SL.DT_DUEDATE) SL) SL 
ON SL.CD_COMPANY = SH.CD_COMPANY AND SL.NO_SO = SH.NO_SO
LEFT JOIN CZ_SA_QTNL QL ON QL.CD_COMPANY = SL.CD_COMPANY AND QL.NO_FILE = SL.NO_SO AND QL.NO_LINE = SL.SEQ_SO
LEFT JOIN MA_CODEDTL MC ON MC.CD_COMPANY = SL.CD_COMPANY AND MC.CD_FIELD = 'CZ_SA00034' AND MC.CD_SYSDEF = SL.TP_STATUS
WHERE SH.CD_COMPANY = @P_CD_COMPANY
AND SH.NO_SO = @P_NO_SO
ORDER BY QL.NO_DSP

GO