ALTER PROCEDURE [NEOE].[SP_CZ_SA_GIRSCH_U]    
(    
    @P_CD_COMPANY			NVARCHAR(7),
    @P_NO_GIR				NVARCHAR(20),
	@P_CD_TYPE				NVARCHAR(3),
	@P_DT_START				NVARCHAR(8),
	@P_DT_COMPLETE			NVARCHAR(8),
	@P_DT_BILL				NVARCHAR(8),
	@P_DC_RMK				NVARCHAR(MAX),
	@P_DC_RMK1				NVARCHAR(MAX),
	@P_DC_RMK2				NVARCHAR(MAX),
	@P_DC_RMK_CI			NVARCHAR(1000),
	@P_DC_RMK_PL			NVARCHAR(1000),
	@P_NO_DELIVERY_EMAIL	NVARCHAR(1000),
	@P_YN_EXCLUDE_CPR		NVARCHAR(1),
	@P_DT_IV				NVARCHAR(8),
	@P_CD_TAEGBAE			NVARCHAR(4),
	@P_ID_UPDATE			NVARCHAR(15)
)    
AS    

IF @P_CD_TYPE = '001'
BEGIN
	UPDATE CZ_SA_GIRH_WORK_DETAIL
	SET DT_START = @P_DT_START,
	    DT_COMPLETE	= @P_DT_COMPLETE,
		DT_BILL	= @P_DT_BILL,
		DC_RMK = @P_DC_RMK,
	    DC_RMK1 = @P_DC_RMK1,
		DC_RMK2 = @P_DC_RMK2,
		DC_RMK_CI = @P_DC_RMK_CI,
		DC_RMK_PL = @P_DC_RMK_PL,
		NO_DELIVERY_EMAIL = @P_NO_DELIVERY_EMAIL,
		YN_EXCLUDE_CPR = @P_YN_EXCLUDE_CPR,
		DT_IV = @P_DT_IV,
		CD_TAEGBAE = @P_CD_TAEGBAE
	WHERE CD_COMPANY = @P_CD_COMPANY
	AND NO_GIR = @P_NO_GIR
END
ELSE
BEGIN
	UPDATE CZ_SA_GIRH_PACK_DETAIL
	SET DT_START = @P_DT_START,
		DT_COMPLETE	= @P_DT_COMPLETE,
		DC_RMK = @P_DC_RMK,
	    DC_RMK1 = @P_DC_RMK1,
		DC_RMK2 = @P_DC_RMK2,
		DC_RMK_CI = @P_DC_RMK_CI
	WHERE CD_COMPANY = @P_CD_COMPANY
	AND NO_GIR = @P_NO_GIR
END



GO