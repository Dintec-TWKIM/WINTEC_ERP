ALTER PROCEDURE [NEOE].[SP_CZ_SA_INQ_RPTL_U]
(
	@P_CD_COMPANY       NVARCHAR(7),
	@P_TP_STEP          NVARCHAR(3),
	@P_NO_KEY			NVARCHAR(20),
	@P_DC_RMK			NVARCHAR(MAX),
	@P_ID_UPDATE		NVARCHAR(15)
) AS	

SET NOCOUNT ON

UPDATE CZ_MA_WORKFLOWH 
SET DC_RMK = @P_DC_RMK,
    UPDATE_HIST = 'SP_CZ_SA_INQ_RPTL_U',
	ID_UPDATE = @P_ID_UPDATE,
	DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
WHERE CD_COMPANY = @P_CD_COMPANY
AND TP_STEP = @P_TP_STEP
AND NO_KEY = @P_NO_KEY

IF @P_TP_STEP = '01'
BEGIN
	UPDATE CZ_MA_WORKFLOWH 
	SET DC_RMK = @P_DC_RMK,
		UPDATE_HIST = 'SP_CZ_SA_INQ_RPTL_U',
		ID_UPDATE = @P_ID_UPDATE,
		DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
	WHERE CD_COMPANY = @P_CD_COMPANY
	AND TP_STEP = '02'
	AND NO_KEY = @P_NO_KEY
END

SET NOCOUNT OFF


GO

