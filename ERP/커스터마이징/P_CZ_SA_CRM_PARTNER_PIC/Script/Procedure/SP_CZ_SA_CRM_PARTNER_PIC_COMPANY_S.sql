ALTER PROCEDURE [NEOE].[SP_CZ_SA_CRM_PARTNER_PIC_COMPANY_S]
(
	@P_CD_COMPANY		NVARCHAR(7),
	@P_CD_PARTNER		NVARCHAR(20),
	@P_SEQ				NUMERIC(5, 0)
) 
AS
 
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT PC.CD_COMPANY,
	   PC.CD_PARTNER,
	   PC.SEQ,
	   PC.NO_INDEX,
	   PC.DT_JOIN,
	   PC.DT_RETIRE,
	   PC.CD_EX_COMPANY,
	   MP.LN_PARTNER AS LN_EX_COMPANY,
	   PC.NM_EX_COMPANY,
	   PC.DC_DEPT,
	   PC.DC_DUTY_RESP,
	   PC.DC_RMK
FROM CZ_CRM_PARTNER_PIC_COMPANY PC
LEFT JOIN MA_PARTNER MP ON MP.CD_COMPANY = PC.CD_COMPANY AND MP.CD_PARTNER = PC.CD_EX_COMPANY
WHERE PC.CD_COMPANY = @P_CD_COMPANY
AND PC.CD_PARTNER = @P_CD_PARTNER
AND PC.SEQ = @P_SEQ

GO

