ALTER PROCEDURE [NEOE].[SP_CZ_SA_CRM_PARTNER_PIC_HULL_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_CRM_PARTNER_PIC_HULL A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY NVARCHAR(7),
			  CD_PARTNER NVARCHAR(20),
			  SEQ		 NUMERIC(5, 0),
			  NO_IMO	 NVARCHAR(7)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.CD_PARTNER = B.CD_PARTNER
AND A.SEQ = B.SEQ
AND A.NO_IMO = B.NO_IMO
-- ================================================== INSERT
INSERT INTO CZ_CRM_PARTNER_PIC_HULL 
(
	CD_COMPANY,
	CD_PARTNER,
	SEQ,
	NO_IMO,
	DC_RMK, 
	ID_INSERT,
	DTS_INSERT
)
SELECT CD_COMPANY,
	   CD_PARTNER,
	   SEQ,
	   NO_IMO,
	   DC_RMK, 
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
  FROM OPENXML (@DOC, '/XML/I', 2) 
          WITH (CD_COMPANY  NVARCHAR(7),
				CD_PARTNER  NVARCHAR(20),
				SEQ			NUMERIC(5, 0),
				NO_IMO		NVARCHAR(7),
				DC_RMK		NVARCHAR(MAX)) 
-- ================================================== UPDATE    
UPDATE A 
   SET A.DC_RMK = B.DC_RMK,
	   A.ID_UPDATE = @P_ID_USER, 
	   A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
  FROM CZ_CRM_PARTNER_PIC_HULL A 
  JOIN OPENXML (@DOC, '/XML/U', 2) 
          WITH (CD_COMPANY  NVARCHAR(7),
				CD_PARTNER  NVARCHAR(20),
				SEQ			NUMERIC(5, 0),
				NO_IMO		NVARCHAR(7),
				DC_RMK		NVARCHAR(MAX)) B 
  ON A.CD_COMPANY = B.CD_COMPANY
  AND A.CD_PARTNER = B.CD_PARTNER
  AND A.SEQ = B.SEQ
  AND A.NO_IMO = B.NO_IMO

EXEC SP_XML_REMOVEDOCUMENT @DOC 




GO

