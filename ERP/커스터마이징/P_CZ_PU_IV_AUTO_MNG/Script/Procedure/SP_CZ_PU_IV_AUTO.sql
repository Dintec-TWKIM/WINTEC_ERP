ALTER PROCEDURE [NEOE].[SP_CZ_PU_IV_AUTO]                
(
	@P_CD_COMPANY           NVARCHAR(7),
    @P_NO_IO                NVARCHAR(MAX),
    @P_DT_PROCESS           NVARCHAR(8),
    @P_DT_PAY_PREARRANGED   NVARCHAR(8),
    @P_ID_USER              NVARCHAR(15)
)                
AS                

DECLARE @V_NO_IV     NVARCHAR(20)
DECLARE @V_ERRMSG	 VARCHAR(255) -- ERROR 메시지
DECLARE @V_DT_IO     NVARCHAR(6)

BEGIN TRAN SP_CZ_PU_IV_AUTO
BEGIN TRY

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SET @V_DT_IO = CONVERT(NVARCHAR(6), GETDATE(), 112)

EXEC NEOE.CP_GETNO @P_CD_COMPANY,'PU', '09', @V_DT_IO,  @V_NO_IV OUTPUT;

WITH A AS
(
    SELECT IH.CD_COMPANY,
           IH.CD_PARTNER,
           IH.FG_TRANS,
           IH.YN_RETURN,
           IL.FG_TPIO,
           IL.FG_TAX,
           IL.CD_EXCH,
           IL.RT_EXCH,
           (SELECT ISNULL(CD_DOCU, (CASE WHEN IH.FG_TRANS = '001' THEN '45' ELSE '46' END)) FROM MA_AISPOSTH WHERE CD_COMPANY = IL.CD_COMPANY AND CD_TP = IL.FG_TPIO AND FG_TP = '200') AS CD_DOCU,
           ROUND(ISNULL(IL.UM_EX, 0) * (ISNULL(IL.QT_IO, 0) - ISNULL(IL.QT_CLS, 0)), 2) AS AM_EX,
           ROUND(ROUND(ISNULL(IL.UM_EX, 0) * (ISNULL(IL.QT_IO, 0) - ISNULL(IL.QT_CLS, 0)), 2) * ISNULL((CASE WHEN EX.CURR_SOUR = '002' THEN EX.RATE_BASE ELSE ROUND(EX.RATE_BASE, 2) END), 1), (CASE WHEN IL.CD_COMPANY = 'S100' THEN 2 ELSE 0 END)) AS AM_K,
           ROUND(ROUND(ROUND(ISNULL(IL.UM_EX, 0) * (ISNULL(IL.QT_IO, 0) - ISNULL(IL.QT_CLS, 0)), 2) * ISNULL((CASE WHEN EX.CURR_SOUR = '002' THEN EX.RATE_BASE ELSE ROUND(EX.RATE_BASE, 2) END), 1), (CASE WHEN IL.CD_COMPANY = 'S100' THEN 2 ELSE 0 END)) * (CONVERT(NUMERIC(17, 4), CASE WHEN ISNULL(MC.CD_FLAG1,'') = '' THEN '0' ELSE MC.CD_FLAG1 END) / 100), (CASE WHEN IL.CD_COMPANY = 'S100' THEN 2 ELSE 0 END)) AS VAT_TAX
    FROM MM_QTIOH IH
    JOIN MM_QTIO IL ON IL.CD_COMPANY = IH.CD_COMPANY AND IL.NO_IO = IH.NO_IO
    LEFT JOIN MA_EXCHANGE EX ON EX.CD_COMPANY = IL.CD_COMPANY AND EX.YYMMDD = IL.DT_IO AND EX.NO_SEQ = '1' AND EX.CURR_SOUR = IL.CD_EXCH AND EX.CURR_DEST = '000'
    LEFT JOIN MA_CODEDTL MC ON MC.CD_COMPANY = IL.CD_COMPANY AND MC.CD_FIELD = 'MA_B000046' AND MC.CD_SYSDEF = IL.FG_TAX
    WHERE IH.CD_COMPANY = @P_CD_COMPANY
    AND EXISTS (SELECT 1
    			FROM STRING_SPLIT(@P_NO_IO, ',')
    			WHERE VALUE = IH.NO_IO)
),
B AS
(
    SELECT A.CD_COMPANY,
           A.CD_PARTNER,
           A.FG_TRANS,
           A.FG_TPIO,
           A.FG_TPIO AS FG_TPPURCHASE,
           A.FG_TAX,
           A.CD_EXCH,
           A.RT_EXCH,
           A.CD_DOCU,
           SUM(CASE WHEN A.YN_RETURN = 'Y' THEN -A.AM_EX ELSE A.AM_EX END) AS AM_EX,
           SUM(CASE WHEN A.YN_RETURN = 'Y' THEN -A.AM_K ELSE A.AM_K END) AS AM_K,
           SUM(CASE WHEN A.YN_RETURN = 'Y' THEN -A.VAT_TAX ELSE A.VAT_TAX END) AS VAT_TAX
    FROM A
    GROUP BY A.CD_COMPANY,
             A.CD_PARTNER,
             A.FG_TRANS,
             A.FG_TPIO,
             A.FG_TAX,
             A.CD_EXCH,
             A.RT_EXCH,
             A.CD_DOCU
)
INSERT INTO PU_IVH
(
    NO_IV,
    CD_COMPANY,
    CD_BIZAREA,
    NO_BIZAREA,
    FG_TPPURCHASE,
    CD_PARTNER,
    FG_TRANS,
    AM_K,
    VAT_TAX,
    DT_PROCESS,         
    FG_TAX,
    TP_FD,
    TP_SUMTAX,
    FG_TAXP,
    CD_DEPT,
    NO_EMP,
    YN_PURSUB,
    YN_EXPIV,
    DTS_INSERT,
    ID_INSERT,
    AM_EX,
    RT_EXCH,
    CD_EXCH,        
    DC_RMK,
    CD_DOCU,
    FG_PAYBILL,
    DT_PAY_PREARRANGED,
    CD_BIZAREA_TAX,
    DT_DUE,
    YN_JEONJA
)
SELECT @V_NO_IV,
       IH.CD_COMPANY,
       MB.CD_BIZAREA,
       MB.NO_BIZAREA,
       IH.FG_TPPURCHASE,
       IH.CD_PARTNER,
       IH.FG_TRANS,
       ISNULL(IH.AM_K, 0) AS AM_K,
       ISNULL(IH.VAT_TAX, 0) AS VAT_TAX,
       @P_DT_PROCESS AS DT_PROCESS,
       IH.FG_TAX,
       'D' AS TP_FD,
       'S' AS TP_SUMTAX,
       '001' AS FG_TAXP, -- 계산서처리 일괄(001), 건별(002)
       ME.CD_DEPT,
       ME.NO_EMP,
       'N' AS YN_PURSUB,
       'N' AS YN_EXPIV,
       NEOE.SF_SYSDATE(GETDATE()) AS DTS_INSERT,
       @P_ID_USER AS ID_INSERT,
       ISNULL(IH.AM_EX, 0) AS AM_EX,
       IH.RT_EXCH,
       IH.CD_EXCH,
       NULL AS DC_RMK,
       (SELECT ISNULL(CD_DOCU, (CASE WHEN IH.FG_TRANS = '001' THEN '45' ELSE '46' END)) FROM MA_AISPOSTH WHERE CD_COMPANY = IH.CD_COMPANY AND CD_TP = IH.FG_TPIO AND FG_TP = '200') AS CD_DOCU,
       MP.FG_PAYBILL,
       @P_DT_PAY_PREARRANGED AS DT_PAY_PREARRANGED,
       MB.CD_BIZAREA AS CD_BIZAREA_TAX,
       @P_DT_PAY_PREARRANGED AS DT_DUE,
       MP.YN_JEONJA  
FROM B AS IH
LEFT JOIN MA_BIZAREA MB ON MB.CD_COMPANY = IH.CD_COMPANY
LEFT JOIN MA_USER MU ON MU.CD_COMPANY = IH.CD_COMPANY AND MU.ID_USER = @P_ID_USER
LEFT JOIN MA_EMP ME ON ME.CD_COMPANY = MU.CD_COMPANY AND ME.NO_EMP = MU.NO_EMP
JOIN MA_PARTNER MP ON MP.CD_COMPANY = IH.CD_COMPANY AND MP.CD_PARTNER = IH.CD_PARTNER

INSERT INTO PU_IVL                
(
    NO_IV,
    NO_LINE,
    CD_COMPANY,
    CD_PLANT,
    NO_IO,
    NO_IOLINE,
    CD_ITEM,                 
    CD_PURGRP,
    CD_CC,
    DT_TAX,
    QT_RCV_CLS,
    UM_ITEM_CLS,
    AM_CLS,
    VAT,
    NO_EMP,
    CD_PJT,                 
    FG_TPPURCHASE,
    NO_PO,
    NO_POLINE,
    CD_EXCH,
    RT_EXCH,
    YN_RETURN,
    UM_EX_CLS,
    AM_EX_CLS,                
    QT_CLS,
    NO_LC,
    NO_LCLINE,
    SEQ_PROJECT,
    NO_WBS,
    NO_CBS,
    TP_UM_TAX
)
SELECT @V_NO_IV,
       ROW_NUMBER() OVER(PARTITION BY IL.CD_COMPANY ORDER BY IL.NO_IO, IL.NO_IOLINE ASC) AS NO_LINE,
       IL.CD_COMPANY,
       IL.CD_PLANT,
       IL.NO_IO,
       IL.NO_IOLINE,
       IL.CD_ITEM,
       PH.CD_PURGRP,
       (CASE WHEN ISNULL(PL.CD_CC,'') = '' THEN PG.CD_CC ELSE PL.CD_CC END) AS CD_CC,
       @P_DT_PROCESS AS DT_TAX,
       (ISNULL(IL.QT_UNIT_MM, 0) - ISNULL(IL.QT_CLS_MM, 0)) AS QT_RCV_CLS,
       (CASE WHEN IH.YN_RETURN = 'Y' THEN -(ROUND(ISNULL(IL.UM_EX, 0) * ISNULL((CASE WHEN EX.CURR_SOUR = '002' THEN EX.RATE_BASE ELSE ROUND(EX.RATE_BASE, 2) END), 1), (CASE WHEN IL.CD_COMPANY = 'S100' THEN 2 ELSE 0 END))) 
	   								 ELSE ROUND(ISNULL(IL.UM_EX, 0) * ISNULL((CASE WHEN EX.CURR_SOUR = '002' THEN EX.RATE_BASE ELSE ROUND(EX.RATE_BASE, 2) END), 1), (CASE WHEN IL.CD_COMPANY = 'S100' THEN 2 ELSE 0 END)) END) AS UM_ITEM_CLS,
       (CASE WHEN IH.YN_RETURN = 'Y' THEN -(ROUND(ROUND(ISNULL(IL.UM_EX, 0) * (ISNULL(IL.QT_IO, 0) - ISNULL(IL.QT_CLS, 0)), 2) * ISNULL((CASE WHEN EX.CURR_SOUR = '002' THEN EX.RATE_BASE ELSE ROUND(EX.RATE_BASE, 2) END), 1), (CASE WHEN IL.CD_COMPANY = 'S100' THEN 2 ELSE 0 END))) 
	   								 ELSE ROUND(ROUND(ISNULL(IL.UM_EX, 0) * (ISNULL(IL.QT_IO, 0) - ISNULL(IL.QT_CLS, 0)), 2) * ISNULL((CASE WHEN EX.CURR_SOUR = '002' THEN EX.RATE_BASE ELSE ROUND(EX.RATE_BASE, 2) END), 1), (CASE WHEN IL.CD_COMPANY = 'S100' THEN 2 ELSE 0 END)) END) AS AM_CLS,
       (CASE WHEN IH.YN_RETURN = 'Y' THEN -(ROUND(ROUND(ROUND(ISNULL(IL.UM_EX, 0) * (ISNULL(IL.QT_IO, 0) - ISNULL(IL.QT_CLS, 0)), 2) * ISNULL((CASE WHEN EX.CURR_SOUR = '002' THEN EX.RATE_BASE ELSE ROUND(EX.RATE_BASE, 2) END), 1), (CASE WHEN IL.CD_COMPANY = 'S100' THEN 2 ELSE 0 END)) * (CONVERT(NUMERIC(17, 4), CASE WHEN ISNULL(MC.CD_FLAG1,'') = '' THEN '0' ELSE MC.CD_FLAG1 END) / 100), (CASE WHEN IL.CD_COMPANY = 'S100' THEN 2 ELSE 0 END))) 
	   								 ELSE ROUND(ROUND(ROUND(ISNULL(IL.UM_EX, 0) * (ISNULL(IL.QT_IO, 0) - ISNULL(IL.QT_CLS, 0)), 2) * ISNULL((CASE WHEN EX.CURR_SOUR = '002' THEN EX.RATE_BASE ELSE ROUND(EX.RATE_BASE, 2) END), 1), (CASE WHEN IL.CD_COMPANY = 'S100' THEN 2 ELSE 0 END)) * (CONVERT(NUMERIC(17, 4), CASE WHEN ISNULL(MC.CD_FLAG1,'') = '' THEN '0' ELSE MC.CD_FLAG1 END) / 100), (CASE WHEN IL.CD_COMPANY = 'S100' THEN 2 ELSE 0 END)) END) AS VAT,
       MU.NO_EMP,
       IL.CD_PJT,                 
       IL.FG_TPIO AS FG_TPPURCHASE,
       IL.NO_PSO_MGMT AS NO_PO,         
	   IL.NO_PSOLINE_MGMT AS NO_POLINE,
       IL.CD_EXCH,
       ISNULL((CASE WHEN EX.CURR_SOUR = '002' THEN EX.RATE_BASE ELSE ROUND(EX.RATE_BASE, 2) END), 1) AS RT_EXCH,
       IH.YN_RETURN,
       (CASE WHEN IH.YN_RETURN = 'Y' THEN -ISNULL(IL.UM_EX, 0) ELSE ISNULL(IL.UM_EX, 0) END) AS UM_EX_CLS,
       (CASE WHEN IH.YN_RETURN = 'Y' THEN -(ROUND(ISNULL(IL.UM_EX, 0) * (ISNULL(IL.QT_IO, 0) - ISNULL(IL.QT_CLS, 0)), 2)) 
	   								 ELSE ROUND(ISNULL(IL.UM_EX, 0) * (ISNULL(IL.QT_IO, 0) - ISNULL(IL.QT_CLS, 0)), 2) END) AS AM_EX_CLS,                
       (ISNULL(IL.QT_IO, 0) - ISNULL(IL.QT_CLS, 0)) AS QT_CLS,
       NULL AS NO_LC,
       0 AS NO_LCLINE,
       0 AS SEQ_PROJECT,
       NULL AS NO_WBS,
       NULL AS NO_CBS,
       (CASE WHEN (ISNULL(IL.TP_UM_TAX, '002') = '002' OR LEN(IL.TP_UM_TAX) < 1) THEN '002' ELSE '001' END) AS TP_UM_TAX
FROM MM_QTIO IL
JOIN MM_QTIOH IH ON IH.CD_COMPANY = IL.CD_COMPANY AND IH.NO_IO = IL.NO_IO
LEFT JOIN PU_POL PL ON PL.CD_COMPANY = IL.CD_COMPANY AND PL.NO_PO = IL.NO_PSO_MGMT AND PL.NO_LINE = IL.NO_PSOLINE_MGMT
LEFT JOIN PU_POH PH ON PH.CD_COMPANY = PL.CD_COMPANY AND PH.NO_PO = PL.NO_PO
LEFT JOIN MA_EXCHANGE EX ON EX.CD_COMPANY = IL.CD_COMPANY AND EX.YYMMDD = IL.DT_IO AND EX.NO_SEQ = '1' AND EX.CURR_SOUR = IL.CD_EXCH AND EX.CURR_DEST = '000'
LEFT JOIN MA_USER MU ON MU.CD_COMPANY = IH.CD_COMPANY AND MU.ID_USER = @P_ID_USER
LEFT JOIN MA_CODEDTL MC ON MC.CD_COMPANY = IL.CD_COMPANY AND MC.CD_FIELD = 'MA_B000046' AND MC.CD_SYSDEF = IL.FG_TAX
LEFT JOIN MA_PURGRP PG ON PG.CD_COMPANY = PH.CD_COMPANY AND PG.CD_PURGRP = PH.CD_PURGRP
WHERE IL.CD_COMPANY = @P_CD_COMPANY
AND EXISTS (SELECT 1
			FROM STRING_SPLIT(@P_NO_IO, ',')
			WHERE VALUE = IL.NO_IO)

SELECT @V_NO_IV

COMMIT TRAN SP_CZ_PU_IV_AUTO

END TRY
BEGIN CATCH
	ROLLBACK TRAN SP_CZ_PU_IV_AUTO
	SELECT @V_ERRMSG = ERROR_MESSAGE()
	GOTO ERROR
END CATCH

RETURN
ERROR: 
RAISERROR(@V_ERRMSG, 16, 1)
ROLLBACK TRAN SP_CZ_PU_IV_AUTO

