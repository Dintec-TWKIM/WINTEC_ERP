ALTER PROCEDURE [NEOE].[SP_CZ_PU_IV_AUTO_MNGL_S]        
(             
	@P_CD_COMPANY           NVARCHAR(7),
	@P_NO_ETAX				NVARCHAR(30)
)        
AS

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT EL.NO_ETAX,
	   EL.SEQ,
	   EL.NM_PART,
	   EL.QT,
	   EL.UM,
	   EL.AM,
	   EL.VAT,
	   EL.DC_RMK
FROM CZ_PU_ETAXL EL
WHERE EL.CD_COMPANY = @P_CD_COMPANY
AND EL.NO_ETAX = @P_NO_ETAX

GO