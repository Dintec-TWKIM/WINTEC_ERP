ALTER PROCEDURE [NEOE].[SP_CZ_PU_IV_AUTOH_U]                
(
	@P_CD_COMPANY           NVARCHAR(7),
	@P_CD_PARTNER			NVARCHAR(20),
	@P_DT_MONTH				NVARCHAR(6),
	@P_IDX					INT,
	@P_DC_RMK				NVARCHAR(500),
	@P_ID_UPDATE			NVARCHAR(15)
)                
AS                

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

UPDATE CZ_PU_ETAX_DETAILH
SET DC_RMK = @P_DC_RMK,
	ID_UPDATE = @P_ID_UPDATE,
	DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
WHERE CD_COMPANY = @P_CD_COMPANY
AND CD_PARTNER = @P_CD_PARTNER
AND DT_MONTH = @P_DT_MONTH
AND IDX = @P_IDX

GO