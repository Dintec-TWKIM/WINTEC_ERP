ALTER PROCEDURE [NEOE].[SP_CZ_SA_BUSINESS_TRIP_SCHEDULE_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_SA_BUSINESS_TRIP_SCHEDULE A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY  NVARCHAR(7),
			  NO_BIZ_TRIP NVARCHAR(20),
			  NO_INDEX	  INT) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.NO_BIZ_TRIP = B.NO_BIZ_TRIP
AND A.NO_INDEX = B.NO_INDEX
-- ================================================== INSERT
INSERT INTO CZ_SA_BUSINESS_TRIP_SCHEDULE 
(
	CD_COMPANY,
	NO_BIZ_TRIP,
	NO_INDEX,
	DT_FROM,
	DT_TO,
	DC_SCHEDULE,
	ID_INSERT,
	DTS_INSERT
)
SELECT CD_COMPANY,
	   NO_BIZ_TRIP,
	   NO_INDEX,
	   DT_FROM,
	   DT_TO,
	   DC_SCHEDULE,
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
  FROM OPENXML (@DOC, '/XML/I', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
				NO_BIZ_TRIP		NVARCHAR(20),
				NO_INDEX		INT,
				DT_FROM			NVARCHAR(8),
				DT_TO			NVARCHAR(8),
				DC_SCHEDULE		NVARCHAR(300)) 
-- ================================================== UPDATE    
UPDATE A 
   SET A.DT_FROM = B.DT_FROM,
	   A.DT_TO = B.DT_TO,
	   A.DC_SCHEDULE = B.DC_SCHEDULE,
	   A.ID_UPDATE = @P_ID_USER, 
	   A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
  FROM CZ_SA_BUSINESS_TRIP_SCHEDULE A 
  JOIN OPENXML (@DOC, '/XML/U', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
				NO_BIZ_TRIP		NVARCHAR(20),
				NO_INDEX		INT,
				DT_FROM			NVARCHAR(8),
				DT_TO			NVARCHAR(8),
				DC_SCHEDULE		NVARCHAR(300)) B 
  ON A.CD_COMPANY = B.CD_COMPANY
  AND A.NO_BIZ_TRIP = B.NO_BIZ_TRIP
  AND A.NO_INDEX = B.NO_INDEX

EXEC SP_XML_REMOVEDOCUMENT @DOC 




GO

