ALTER PROCEDURE [NEOE].[SP_CZ_SA_BUSINESS_TRIP_MEETING_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_SA_BUSINESS_TRIP_MEETING A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY  NVARCHAR(7),
			  NO_BIZ_TRIP NVARCHAR(20),
			  NO_MEETING  NVARCHAR(20)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.NO_BIZ_TRIP = B.NO_BIZ_TRIP
AND A.NO_MEETING = B.NO_MEETING
-- ================================================== INSERT
INSERT INTO CZ_SA_BUSINESS_TRIP_MEETING 
(
	CD_COMPANY,
	NO_BIZ_TRIP,
	NO_INDEX,
	NO_MEETING,
	ID_INSERT,
	DTS_INSERT
)
SELECT CD_COMPANY,
	   NO_BIZ_TRIP,
	   NO_INDEX,
	   NO_MEETING,
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
  FROM OPENXML (@DOC, '/XML/I', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
				NO_BIZ_TRIP		NVARCHAR(20),
				NO_INDEX		NUMERIC(18, 0),
				NO_MEETING		NVARCHAR(20)) 
-- ================================================== UPDATE    
UPDATE A 
   SET A.NO_MEETING = B.NO_MEETING,
	   A.ID_UPDATE = @P_ID_USER, 
	   A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
  FROM CZ_SA_BUSINESS_TRIP_MEETING A 
  JOIN OPENXML (@DOC, '/XML/U', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
				NO_BIZ_TRIP		NVARCHAR(20),
				NO_INDEX		NUMERIC(18, 0),
				NO_MEETING		NVARCHAR(20)) B 
  ON A.CD_COMPANY = B.CD_COMPANY
  AND A.NO_BIZ_TRIP = B.NO_BIZ_TRIP
  AND A.NO_INDEX = B.NO_INDEX

EXEC SP_XML_REMOVEDOCUMENT @DOC 




GO

