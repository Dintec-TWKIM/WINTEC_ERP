ALTER PROCEDURE [NEOE].[SP_CZ_SA_BUSINESS_TRIP_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
INSERT INTO CZ_SA_BUSINESS_TRIP_LOG
(
	CD_COMPANY,
	NO_BIZ_TRIP,
	SEQ,
	DT_START,
	DT_END,
	DC_DATE,
	DC_LOCATION,
	DC_PURPOSE,
	DC_START,
	DC_END,
	TP_LOG,
	ID_LOG,
	DTS_LOG
)
SELECT A.CD_COMPANY,
	   A.NO_BIZ_TRIP,
	   (SELECT ISNULL(MAX(SEQ), 0) 
	    FROM CZ_SA_BUSINESS_TRIP_LOG
		WHERE CD_COMPANY = A.CD_COMPANY
		AND NO_BIZ_TRIP = A.NO_BIZ_TRIP) + ROW_NUMBER() OVER (PARTITION BY A.CD_COMPANY, A.NO_BIZ_TRIP ORDER BY A.CD_COMPANY, A.NO_BIZ_TRIP) AS SEQ,
	   A.DT_START,
	   A.DT_END,
	   A.DC_DATE,
	   A.DC_LOCATION,
	   A.DC_PURPOSE,
	   A.DC_START,
	   A.DC_END,
	   'D' AS TP_LOG,
	   @P_ID_USER AS ID_LOG,
	   NEOE.SF_SYSDATE(GETDATE()) AS DTS_LOG
FROM CZ_SA_BUSINESS_TRIP A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY  NVARCHAR(7),
			  NO_BIZ_TRIP NVARCHAR(20)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.NO_BIZ_TRIP = B.NO_BIZ_TRIP

DELETE A 
FROM CZ_SA_BUSINESS_TRIP A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY  NVARCHAR(7),
			  NO_BIZ_TRIP NVARCHAR(20)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.NO_BIZ_TRIP = B.NO_BIZ_TRIP

DELETE A 
FROM CZ_SA_BUSINESS_TRIP_SCHEDULE A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY  NVARCHAR(7),
			  NO_BIZ_TRIP NVARCHAR(20)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.NO_BIZ_TRIP = B.NO_BIZ_TRIP

DELETE A 
FROM CZ_SA_BUSINESS_TRIP_EXPENSE A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY  NVARCHAR(7),
			  NO_BIZ_TRIP NVARCHAR(20)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.NO_BIZ_TRIP = B.NO_BIZ_TRIP

DELETE A 
FROM CZ_SA_BUSINESS_TRIP_MEETING A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY  NVARCHAR(7),
			  NO_BIZ_TRIP NVARCHAR(20)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.NO_BIZ_TRIP = B.NO_BIZ_TRIP

-- ================================================== INSERT
INSERT INTO CZ_SA_BUSINESS_TRIP 
(
	CD_COMPANY,
	NO_BIZ_TRIP,
	DT_START,
	DT_END,
	DC_DATE,
	DC_LOCATION,
	DC_PURPOSE,
	DC_START,
	DC_END,
	ID_INSERT,
	DTS_INSERT
)
SELECT CD_COMPANY,
	   NO_BIZ_TRIP,
	   DT_START,
	   DT_END,
	   DC_DATE,
	   DC_LOCATION,
	   REPLACE(DC_PURPOSE, CHAR(10), CHAR(13) + CHAR(10)),
	   REPLACE(DC_START, CHAR(10), CHAR(13) + CHAR(10)),
	   REPLACE(DC_END, CHAR(10), CHAR(13) + CHAR(10)),
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
  FROM OPENXML (@DOC, '/XML/I', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
				NO_BIZ_TRIP		NVARCHAR(20),
				DT_START		NVARCHAR(8),
				DT_END			NVARCHAR(8),
				DC_DATE			NVARCHAR(100),
				DC_LOCATION		NVARCHAR(300),
				DC_PURPOSE		NVARCHAR(500),
				DC_START		NVARCHAR(MAX),
				DC_END			NVARCHAR(MAX)) 

INSERT INTO CZ_SA_BUSINESS_TRIP_LOG
(
	CD_COMPANY,
	NO_BIZ_TRIP,
	SEQ,
	DT_START,
	DT_END,
	DC_DATE,
	DC_LOCATION,
	DC_PURPOSE,
	DC_START,
	DC_END,
	TP_LOG,
	ID_LOG,
	DTS_LOG
)
SELECT A.CD_COMPANY,
	   A.NO_BIZ_TRIP,
	   (SELECT ISNULL(MAX(SEQ), 0) 
	    FROM CZ_SA_BUSINESS_TRIP_LOG
		WHERE CD_COMPANY = A.CD_COMPANY
		AND NO_BIZ_TRIP = A.NO_BIZ_TRIP) + ROW_NUMBER() OVER (PARTITION BY A.CD_COMPANY, A.NO_BIZ_TRIP ORDER BY A.CD_COMPANY, A.NO_BIZ_TRIP) AS SEQ,
	   A.DT_START,
	   A.DT_END,
	   A.DC_DATE,
	   A.DC_LOCATION,
	   A.DC_PURPOSE,
	   A.DC_START,
	   A.DC_END,
	   'I' AS TP_LOG,
	   @P_ID_USER AS ID_LOG,
	   NEOE.SF_SYSDATE(GETDATE()) AS DTS_LOG
FROM CZ_SA_BUSINESS_TRIP A 
JOIN OPENXML (@DOC, '/XML/I', 2) 
        WITH (CD_COMPANY  NVARCHAR(7),
			  NO_BIZ_TRIP NVARCHAR(20)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.NO_BIZ_TRIP = B.NO_BIZ_TRIP
-- ================================================== UPDATE    
UPDATE A 
   SET A.DT_START = B.DT_START,
	   A.DT_END = B.DT_END,
	   A.DC_DATE = B.DC_DATE,
	   A.DC_LOCATION = B.DC_LOCATION,
	   A.DC_PURPOSE = REPLACE(B.DC_PURPOSE, CHAR(10), CHAR(13) + CHAR(10)),
	   A.DC_START = REPLACE(B.DC_START, CHAR(10), CHAR(13) + CHAR(10)),
	   A.DC_END = REPLACE(B.DC_END, CHAR(10), CHAR(13) + CHAR(10)),
	   A.ID_UPDATE = @P_ID_USER, 
	   A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
  FROM CZ_SA_BUSINESS_TRIP A 
  JOIN OPENXML (@DOC, '/XML/U', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
				NO_BIZ_TRIP		NVARCHAR(20),
				DT_START		NVARCHAR(8),
				DT_END			NVARCHAR(8),
				DC_DATE			NVARCHAR(100),
				DC_LOCATION		NVARCHAR(300),
				DC_PURPOSE		NVARCHAR(500),
				DC_START		NVARCHAR(MAX),
				DC_END			NVARCHAR(MAX)) B 
  ON A.CD_COMPANY = B.CD_COMPANY
  AND A.NO_BIZ_TRIP = B.NO_BIZ_TRIP

INSERT INTO CZ_SA_BUSINESS_TRIP_LOG
(
	CD_COMPANY,
	NO_BIZ_TRIP,
	SEQ,
	DT_START,
	DT_END,
	DC_DATE,
	DC_LOCATION,
	DC_PURPOSE,
	DC_START,
	DC_END,
	TP_LOG,
	ID_LOG,
	DTS_LOG
)
SELECT A.CD_COMPANY,
	   A.NO_BIZ_TRIP,
	   (SELECT ISNULL(MAX(SEQ), 0) 
	    FROM CZ_SA_BUSINESS_TRIP_LOG
		WHERE CD_COMPANY = A.CD_COMPANY
		AND NO_BIZ_TRIP = A.NO_BIZ_TRIP) + ROW_NUMBER() OVER (PARTITION BY A.CD_COMPANY, A.NO_BIZ_TRIP ORDER BY A.CD_COMPANY, A.NO_BIZ_TRIP) AS SEQ,
	   A.DT_START,
	   A.DT_END,
	   A.DC_DATE,
	   A.DC_LOCATION,
	   A.DC_PURPOSE,
	   A.DC_START,
	   A.DC_END,
	   'U' AS TP_LOG,
	   @P_ID_USER AS ID_LOG,
	   NEOE.SF_SYSDATE(GETDATE()) AS DTS_LOG
FROM CZ_SA_BUSINESS_TRIP A 
JOIN OPENXML (@DOC, '/XML/U', 2) 
        WITH (CD_COMPANY  NVARCHAR(7),
			  NO_BIZ_TRIP NVARCHAR(20)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.NO_BIZ_TRIP = B.NO_BIZ_TRIP

EXEC SP_XML_REMOVEDOCUMENT @DOC 




