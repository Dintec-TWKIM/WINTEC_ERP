ALTER PROCEDURE [NEOE].[SP_CZ_SA_UNINV_MNGL_U]
( 
	@P_CD_COMPANY			NVARCHAR(7),
	@P_NO_SO				NVARCHAR(20),
	@P_SEQ_SO				NUMERIC(5, 0),
	@P_NO_IO				NVARCHAR(20),
	@P_NO_IOLINE			NUMERIC(5, 0),
	@P_YN_AM				NVARCHAR(1),
	@P_TP_GI				NVARCHAR(3),
	@P_DC2					NVARCHAR(200),
	@P_DC_RMK1				NVARCHAR(200),
	@P_ID_UPDATE			NVARCHAR(15)
)
AS

UPDATE SA_SOL
SET TP_GI = @P_TP_GI,
	DC2 = @P_DC2,
	ID_UPDATE = @P_ID_UPDATE,
	DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
WHERE CD_COMPANY = @P_CD_COMPANY
AND NO_SO = @P_NO_SO
AND SEQ_SO = @P_SEQ_SO

UPDATE MM_QTIO
SET YN_AM = @P_YN_AM,
	DC_RMK1 = @P_DC_RMK1,
	ID_UPDATE = @P_ID_UPDATE,
	DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
WHERE CD_COMPANY = @P_CD_COMPANY
AND NO_IO = @P_NO_IO
AND NO_IOLINE = @P_NO_IOLINE

GO