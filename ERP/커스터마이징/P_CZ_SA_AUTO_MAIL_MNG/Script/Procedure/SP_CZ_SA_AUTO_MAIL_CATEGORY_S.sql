ALTER PROCEDURE [NEOE].[SP_CZ_SA_AUTO_MAIL_CATEGORY_S]    
(            
	@P_CD_COMPANY		NVARCHAR(7),
	@P_CD_PARTNER		NVARCHAR(20)
)            
AS

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT AC.CD_COMPANY,
	   AC.CD_PARTNER,
	   AC.CD_CATEGORY1,
	   AC.CD_CATEGORY2,
