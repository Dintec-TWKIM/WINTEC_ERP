ALTER PROCEDURE [NEOE].[SP_CZ_PU_ADPAYMENT_BILL_LD]
(
	@P_CD_COMPANY     NVARCHAR(7), 
	@P_NO_BILLS       NVARCHAR(20), 
	@P_NO_LINE        NUMERIC(3,0)
) 
AS 
BEGIN
    DECLARE
	@ERRNO               INT,
    @ERRMSG              VARCHAR(255)

	DELETE CZ_PU_ADPAYMENT_BILL_L
	WHERE CD_COMPANY = @P_CD_COMPANY
	AND NO_BILLS = @P_NO_BILLS
	AND NO_LINE = @P_NO_LINE

IF (@@ERROR <> 0 )
BEGIN  
      SELECT @ERRNO  = 100000,  
                   @ERRMSG = 'CM_M100010'  
      GOTO ERROR  
END

RETURN
ERROR:  
    ROLLBACK TRAN
    RAISERROR (@ERRMSG, 18 , 1)  
END
GO

