ALTER PROCEDURE [NEOE].[SP_CZ_SA_PACK_REGH_I]
(
	@P_CD_COMPANY			NVARCHAR(7), 
	@P_NO_GIR				NVARCHAR(20),
	@P_NO_PACK				NUMERIC(5, 0), 
	@P_NM_PACK				NUMERIC(5, 0), 
	@P_DT_PACK				NVARCHAR(8), 
	@P_CD_TYPE				NVARCHAR(3), 
	@P_CD_SIZE				NVARCHAR(3),
	@P_QT_NET_WEIGHT		NUMERIC(17, 4), 
	@P_QT_GROSS_WEIGHT		NUMERIC(17, 4),
	@P_QT_WIDTH				NUMERIC(17, 4),
	@P_QT_LEGTH				NUMERIC(17, 4),
	@P_QT_HEIGHT			NUMERIC(17, 4),
	@P_DC_RMK				NVARCHAR(MAX), 
	@P_ID_INSERT			NVARCHAR(15)
) 
AS
INSERT INTO CZ_SA_PACKH
(
	CD_COMPANY,
	NO_GIR,				
	NO_PACK,
	NM_PACK,	
	DT_PACK,		
	CD_TYPE,	
	CD_SIZE,			
	QT_NET_WEIGHT,		
	QT_GROSS_WEIGHT,			
	QT_WIDTH,			
	QT_LENGTH,				
	QT_HEIGHT,			
	DC_RMK,
	YN_OUTSOURCING,
	NO_GIR_ORG,
	NO_PACK_ORG,
	ID_INSERT,
	DTS_INSERT
)
VALUES 
(
	@P_CD_COMPANY,
	@P_NO_GIR,				
	@P_NO_PACK,
	@P_NM_PACK,		
	@P_DT_PACK,	
	@P_CD_TYPE,		
	@P_CD_SIZE,		
	@P_QT_NET_WEIGHT,		
	@P_QT_GROSS_WEIGHT,			
	@P_QT_WIDTH,			
	@P_QT_LEGTH,				
	@P_QT_HEIGHT,			
	@P_DC_RMK,
	(CASE WHEN @P_ID_INSERT LIKE 'A-%' THEN 'Y' ELSE 'N' END),
	@P_NO_GIR,				
	@P_NO_PACK,
	@P_ID_INSERT,			
	NEOE.SF_SYSDATE(GETDATE())
)

IF EXISTS (SELECT 1
		   FROM CZ_SA_GIRL_PACK GL
		   LEFT JOIN CZ_SA_PACKL PL ON PL.CD_COMPANY = GL.CD_COMPANY AND PL.NO_GIR = GL.NO_GIR AND PL.SEQ_GIR = GL.SEQ_GIR
		   WHERE GL.CD_COMPANY = @P_CD_COMPANY
		   AND GL.NO_GIR = @P_NO_GIR
		   AND GL.CD_ITEM NOT LIKE 'SD%'
		   GROUP BY GL.CD_COMPANY, GL.NO_GIR
		   HAVING SUM(GL.QT_GIR) = SUM(PL.QT_PACK)
		   AND COUNT(DISTINCT GL.SEQ_GIR) = COUNT(DISTINCT PL.SEQ_GIR))
BEGIN
	UPDATE CZ_SA_GIRH_PACK
	SET STA_GIR = 'C',
		ID_UPDATE = @P_ID_INSERT,
		DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
	WHERE CD_COMPANY = @P_CD_COMPANY
	AND NO_GIR = @P_NO_GIR	
END

IF EXISTS (SELECT 1 FROM CZ_SA_GIRH_PACK_DETAIL WHERE CD_COMPANY = @P_CD_COMPANY AND NO_GIR = @P_NO_GIR)
BEGIN
	UPDATE CZ_SA_GIRH_PACK_DETAIL
	SET DT_PACKING = NEOE.SF_SYSDATE(GETDATE()),
	    NO_PACK_EMP = @P_ID_INSERT,
		DC_RESULT = STUFF((SELECT ' SIZE:'
								  + (CASE CD_TYPE WHEN '002' THEN '(P)' WHEN '003' THEN '(W)' ELSE '' END)
								  + FORMAT(QT_WIDTH, 'g15')
								  + ' X ' 
								  + FORMAT(QT_LENGTH, 'g15')
								  + ' X '
								  + FORMAT(QT_HEIGHT, 'g15')
								  + ' (MM), KG:'
								  + FORMAT(QT_GROSS_WEIGHT, 'g15')
								  + CHAR(10)
						   FROM CZ_SA_PACKH
						   WHERE CD_COMPANY = @P_CD_COMPANY
						   AND NO_GIR = @P_NO_GIR 
						   FOR XML PATH(''), TYPE).value('.', 'nvarchar(max)'), 1, 1, '')
	WHERE CD_COMPANY = @P_CD_COMPANY
	AND NO_GIR = @P_NO_GIR
END
ELSE
BEGIN
	UPDATE CZ_SA_GIRH_WORK_DETAIL
	SET DT_PACKING = NEOE.SF_SYSDATE(GETDATE()),
	    NO_PACK_EMP = @P_ID_INSERT,
		DC_RESULT = STUFF((SELECT ' SIZE:'
								  + (CASE CD_TYPE WHEN '002' THEN '(P)' WHEN '003' THEN '(W)' ELSE '' END)
								  + FORMAT(QT_WIDTH, 'g15')
								  + ' X ' 
								  + FORMAT(QT_LENGTH, 'g15')
								  + ' X '
								  + FORMAT(QT_HEIGHT, 'g15')
								  + ' (MM), KG:'
								  + FORMAT(QT_GROSS_WEIGHT, 'g15')
								  + CHAR(10)
						   FROM CZ_SA_PACKH
						   WHERE CD_COMPANY = @P_CD_COMPANY
						   AND NO_GIR = @P_NO_GIR 
						   FOR XML PATH(''), TYPE).value('.', 'nvarchar(max)'), 1, 1, '')
	WHERE CD_COMPANY = @P_CD_COMPANY
	AND NO_GIR = @P_NO_GIR

	EXEC SP_CZ_SA_PACK_REG_MSG @P_CD_COMPANY, @P_NO_GIR
END

