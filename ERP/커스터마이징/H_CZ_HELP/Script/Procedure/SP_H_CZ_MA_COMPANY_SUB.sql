ALTER PROCEDURE [NEOE].[SP_H_CZ_MA_COMPANY_SUB]
(
	@P_CD_COMPANY	   NVARCHAR(7),
	@P_SEARCH		   NVARCHAR(MAX)
) 
AS

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT CD_COMPANY, NM_COMPANY 
FROM MA_COMPANY
WHERE (@P_CD_COMPANY = 'K300' OR CD_COMPANY <> 'K300')
AND (ISNULL(@P_SEARCH, '') = '' OR NM_COMPANY LIKE '%' + @P_SEARCH + '%')
ORDER BY CD_COMPANY

GO