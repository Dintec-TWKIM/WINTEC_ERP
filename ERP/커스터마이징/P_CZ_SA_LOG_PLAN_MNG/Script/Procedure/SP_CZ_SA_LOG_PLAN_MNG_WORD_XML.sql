ALTER PROCEDURE [NEOE].[SP_CZ_SA_LOG_PLAN_MNG_WORD_XML]  
(
    @P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
)
AS

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_SA_LOG_PLAN_WORD A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY	NVARCHAR(7),
			  SEQ			INT) B 
ON A.CD_COMPANY = B.CD_COMPANY 
AND A.SEQ = B.SEQ
-- ================================================== INSERT
INSERT INTO CZ_SA_LOG_PLAN_WORD 
(
    CD_COMPANY,
	DC_WORD,
	ID_INSERT,
	DTS_INSERT
)
SELECT CD_COMPANY,
       REPLACE(DC_WORD, CHAR(10), CHAR(13) + CHAR(10)),
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
  FROM OPENXML (@DOC, '/XML/I', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
				DC_WORD			NVARCHAR(100))
-- ================================================== UPDATE    
UPDATE A 
 SET A.DC_WORD = REPLACE(B.DC_WORD, CHAR(10), CHAR(13) + CHAR(10))
FROM CZ_SA_LOG_PLAN_WORD A 
JOIN OPENXML (@DOC, '/XML/U', 2) 
        WITH (CD_COMPANY		NVARCHAR(7),
			SEQ				INT,
			DC_WORD			NVARCHAR(100)) B 
ON A.CD_COMPANY = B.CD_COMPANY 
AND A.SEQ = B.SEQ

EXEC SP_XML_REMOVEDOCUMENT @DOC 

GO