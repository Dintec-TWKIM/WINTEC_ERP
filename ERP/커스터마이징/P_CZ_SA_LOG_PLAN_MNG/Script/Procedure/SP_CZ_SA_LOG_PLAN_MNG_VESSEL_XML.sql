ALTER PROCEDURE [NEOE].[SP_CZ_SA_LOG_PLAN_MNG_VESSEL_XML]  
(
    @P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
)
AS

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_SA_LOG_PLAN_VESSEL A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY	NVARCHAR(7),
			  NO_REV		INT,
			  NO_IDX		INT) B 
ON A.CD_COMPANY = B.CD_COMPANY 
AND A.NO_REV = B.NO_REV
AND A.NO_IDX = B.NO_IDX

DELETE A 
FROM CZ_SA_LOG_PLAN_SHIP A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY	NVARCHAR(7),
			  NO_REV 		INT,
			  NO_IDX		INT) B 
ON A.CD_COMPANY = B.CD_COMPANY 
AND A.NO_REV = B.NO_REV
AND A.NO_IDX = B.NO_IDX
-- ================================================== INSERT
INSERT INTO CZ_SA_LOG_PLAN_VESSEL
(
    CD_COMPANY,
	NO_REV,
	NO_IDX,
	NM_VESSEL,
    NO_IMO,
	DC_LOCATION,
    DC_PORT,
    DC_EMP,
    DC_ETB,
    DC_ETD,
    DT_COMPLETE,
    DC_CAR,
    DC_ITEM,
    DC_TEL,
    DC_ETC,
	NO_GIR,
	DC_SORT,
	ID_INSERT,
	DTS_INSERT
)
SELECT CD_COMPANY,
	   NO_REV,
	   NO_IDX,
	   NM_VESSEL,
       NO_IMO,
	   DC_LOCATION,
       DC_PORT,
       REPLACE(DC_EMP, CHAR(10), CHAR(13) + CHAR(10)) AS DC_ITEM,
       DC_ETB,
       DC_ETD,
       DT_COMPLETE,
       DC_CAR,
	   REPLACE(DC_ITEM, CHAR(10), CHAR(13) + CHAR(10)) AS DC_ITEM,
       DC_TEL,
	   REPLACE(DC_ETC, CHAR(10), CHAR(13) + CHAR(10)) AS DC_ETC,
	   REPLACE(NO_GIR, CHAR(10), CHAR(13) + CHAR(10)) AS NO_GIR,
	   DC_SORT,
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
  FROM OPENXML (@DOC, '/XML/I', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
				NO_REV			INT,
				NO_IDX			INT,
				NM_VESSEL		NVARCHAR(50),
				NO_IMO			NVARCHAR(10),
				DC_LOCATION		NVARCHAR(20),
				DC_PORT			NVARCHAR(20),
				DC_EMP			NVARCHAR(100),
				DC_ETB			NVARCHAR(10),
				DC_ETD			NVARCHAR(10),
				DT_COMPLETE		NVARCHAR(8),
				DC_CAR			NVARCHAR(50),
				DC_ITEM			NVARCHAR(MAX),
				DC_TEL			NVARCHAR(10),
				DC_ETC			NVARCHAR(MAX),
				NO_GIR			NVARCHAR(1000),
				DC_SORT			NVARCHAR(10)) 
-- ================================================== UPDATE    
UPDATE A 
   SET A.NM_VESSEL = B.NM_VESSEL,
	   A.DC_LOCATION = B.DC_LOCATION,
	   A.DC_PORT = B.DC_PORT,
	   A.DC_EMP = REPLACE(B.DC_EMP, CHAR(10), CHAR(13) + CHAR(10)),
	   A.DC_ETB = B.DC_ETB,
	   A.DC_ETD = B.DC_ETD,
	   A.DT_COMPLETE = B.DT_COMPLETE,
	   A.DC_CAR = B.DC_CAR,
	   A.DC_ITEM = REPLACE(B.DC_ITEM, CHAR(10), CHAR(13) + CHAR(10)),
	   A.DC_TEL = B.DC_TEL,
	   A.DC_ETC = REPLACE(B.DC_ETC, CHAR(10), CHAR(13) + CHAR(10)),
	   A.NO_GIR = REPLACE(B.NO_GIR, CHAR(10), CHAR(13) + CHAR(10)),
	   A.DC_SORT = B.DC_SORT,
	   A.ID_UPDATE = @P_ID_USER, 
	   A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
  FROM CZ_SA_LOG_PLAN_VESSEL A 
  JOIN OPENXML (@DOC, '/XML/U', 2) 
          WITH (CD_COMPANY		NVARCHAR(7),
				NO_REV			INT,
				NO_IDX			INT,
				NM_VESSEL		NVARCHAR(50),
				NO_IMO			NVARCHAR(10),
				DC_LOCATION		NVARCHAR(20),
				DC_PORT			NVARCHAR(20),
				DC_EMP			NVARCHAR(100),
				DC_ETB			NVARCHAR(10),
				DC_ETD			NVARCHAR(10),
				DT_COMPLETE		NVARCHAR(8),
				DC_CAR			NVARCHAR(50),
				DC_ITEM			NVARCHAR(MAX),
				DC_TEL			NVARCHAR(10),
				DC_ETC			NVARCHAR(MAX),
				NO_GIR			NVARCHAR(200),
				DC_SORT			NVARCHAR(10)) B 
  ON A.CD_COMPANY = B.CD_COMPANY 
  AND A.NO_REV = B.NO_REV
  AND A.NO_IDX = B.NO_IDX

EXEC SP_XML_REMOVEDOCUMENT @DOC 

GO