ALTER PROCEDURE [NEOE].[SP_CZ_SA_FORWARD_OUT_MNG_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_SA_FORWARD_OUT A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY	NVARCHAR(7),
		      NO_GIR		NVARCHAR(20),
			  NO_PACK		NUMERIC(5, 0)) B 
ON A.CD_COMPANY = B.CD_COMPANY 
AND A.NO_GIR = B.NO_GIR
AND A.NO_PACK = B.NO_PACK
-- ================================================== INSERT
INSERT INTO CZ_SA_FORWARD_OUT 
(
	CD_COMPANY,
	CD_PARTNER,
	DT_FORWARD,
	NO_GIR,
	NO_PACK,
	DC_RMK,
	ID_INSERT,
	DTS_INSERT
)
SELECT A.CD_COMPANY,
	   A.CD_PARTNER,
	   A.DT_FORWARD,
	   A.NO_GIR,
	   A.NO_PACK,
	   DC_RMK,
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
FROM OPENXML (@DOC, '/XML/I', 2) 
     WITH (CD_COMPANY		NVARCHAR(7), 
		   CD_PARTNER		NVARCHAR(20),
		   DT_FORWARD		NVARCHAR(8),
		   NO_GIR		    NVARCHAR(20),
		   NO_PACK			NUMERIC(5, 0),
		   DC_RMK			NVARCHAR(200)) A

-- ================================================== UPDATE
UPDATE A
SET A.CD_PARTNER = B.CD_PARTNER,
	A.DT_FORWARD = B.DT_FORWARD,
	A.DC_RMK = B.DC_RMK,
	A.ID_UPDATE = @P_ID_USER,
	A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
FROM CZ_SA_FORWARD_OUT A
JOIN OPENXML (@DOC, '/XML/U', 2) 
        WITH (CD_COMPANY		NVARCHAR(7), 
			  NO_GIR		    NVARCHAR(20),
			  NO_PACK			NUMERIC(5, 0),
			  CD_PARTNER		NVARCHAR(20),
			  DT_FORWARD		NVARCHAR(8),
			  DC_RMK			NVARCHAR(200)) B
ON B.CD_COMPANY = A.CD_COMPANY 
AND B.NO_GIR = A.NO_GIR
AND B.NO_PACK = A.NO_PACK

EXEC SP_XML_REMOVEDOCUMENT @DOC 

GO