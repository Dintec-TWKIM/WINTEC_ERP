ALTER PROCEDURE [NEOE].[SP_CZ_PR_WO_SA_SOL_CHANGE_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS 

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_PR_SA_SOL_PR_WO_MAPPING A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY	NVARCHAR(7),
			  CD_PLANT		NVARCHAR(7),
			  NO_SO			NVARCHAR(20),
			  NO_LINE		NUMERIC(5, 0),
			  NO_WO			NVARCHAR(20)) B 
ON A.CD_COMPANY = B.CD_COMPANY
AND A.CD_PLANT = B.CD_PLANT
AND A.NO_SO = B.NO_SO
AND A.NO_LINE = B.NO_LINE
AND A.NO_WO = B.NO_WO
-- ================================================== INSERT
INSERT INTO CZ_PR_SA_SOL_PR_WO_MAPPING 
(
	CD_COMPANY,
	CD_PLANT,
	NO_SO,
	NO_LINE,
	NO_WO,
	CD_ITEM,
	QT_APPLY,
	ID_INSERT,
	DTS_INSERT
)
SELECT CD_COMPANY,
	   CD_PLANT,
	   NO_SO,
	   NO_LINE,
	   NO_WO,
	   CD_ITEM,
	   QT_APPLY,
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
  FROM OPENXML (@DOC, '/XML/I', 2) 
          WITH (CD_COMPANY	NVARCHAR(7),
			    CD_PLANT	NVARCHAR(7),
			    NO_SO		NVARCHAR(20),
			    NO_LINE		NUMERIC(5, 0),
			    NO_WO		NVARCHAR(20),
				CD_ITEM		NVARCHAR(20),
				QT_APPLY	NUMERIC(17, 4))

EXEC SP_XML_REMOVEDOCUMENT @DOC 

GO

