ALTER PROCEDURE [NEOE].[SP_CZ_MA_DOCUMENT_MNG_WINTEC_XML] 
(
	@P_CD_COMPANY	NVARCHAR(7),
	@P_CD_PLANT		NVARCHAR(20),
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_MA_DRAWING_WINTEC A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (SEQ		  NUMERIC(5, 0)) B 
ON A.CD_COMPANY = @P_CD_COMPANY
AND A.CD_PLANT = @P_CD_PLANT
AND A.SEQ = B.SEQ

-- ================================================== INSERT
INSERT INTO CZ_MA_DRAWING_WINTEC 
(
	CD_COMPANY,
    CD_PLANT,
	SEQ,
	TP_DRAWING,
	NO_DRAWING,
	CD_ITEM,
	DC_RMK,
    ID_INSERT,
    DTS_INSERT
)
SELECT @P_CD_COMPANY,
	   @P_CD_PLANT,
	   SEQ,
	   TP_DRAWING,
	   NO_DRAWING,
	   CD_ITEM,
	   DC_RMK,
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
FROM OPENXML (@DOC, '/XML/I', 2) 
        WITH (SEQ		    NUMERIC(5, 0),
			  TP_DRAWING	NVARCHAR(4),
			  NO_DRAWING	NVARCHAR(100),
			  CD_ITEM		NVARCHAR(20),
			  DC_RMK		NVARCHAR(MAX))
-- ================================================== UPDATE    
UPDATE A 
   SET A.TP_DRAWING = B.TP_DRAWING,
	   A.NO_DRAWING = B.NO_DRAWING,
	   A.CD_ITEM = B.CD_ITEM,
	   A.DC_RMK = B.DC_RMK,
	   A.ID_UPDATE = @P_ID_USER, 
	   A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
  FROM CZ_MA_DRAWING_WINTEC A 
  JOIN OPENXML (@DOC, '/XML/U', 2) 
          WITH (SEQ		    NUMERIC(5, 0),
				TP_DRAWING	NVARCHAR(4),
				NO_DRAWING	NVARCHAR(100),
				CD_ITEM		NVARCHAR(20),
			    DC_RMK		NVARCHAR(MAX)) B 
  ON A.CD_COMPANY = @P_CD_COMPANY
  AND A.CD_PLANT = @P_CD_PLANT
  AND A.SEQ = B.SEQ

EXEC SP_XML_REMOVEDOCUMENT @DOC 

GO

