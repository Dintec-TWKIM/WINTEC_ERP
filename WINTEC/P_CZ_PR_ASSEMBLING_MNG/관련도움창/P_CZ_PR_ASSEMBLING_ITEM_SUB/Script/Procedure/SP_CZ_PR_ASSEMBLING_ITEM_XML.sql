ALTER PROCEDURE [NEOE].[SP_CZ_PR_ASSEMBLING_ITEM_XML] 
(
	@P_XML			XML, 
	@P_ID_USER		NVARCHAR(10), 
    @DOC			INT = NULL
) 
AS

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

EXEC SP_XML_PREPAREDOCUMENT @DOC OUTPUT, @P_XML 

-- ================================================== DELETE
DELETE A 
FROM CZ_PR_ASSEMBLING_ITEM A 
JOIN OPENXML (@DOC, '/XML/D', 2) 
        WITH (CD_COMPANY	NVARCHAR(7),
			  CD_PLANT		NVARCHAR(7),
			  CD_ITEM		NVARCHAR(20),
			  CD_PITEM		NVARCHAR(20)) B 
 ON A.CD_COMPANY = B.CD_COMPANY
AND A.CD_PLANT = B.CD_PLANT
AND A.CD_ITEM = B.CD_ITEM
AND A.CD_PITEM = B.CD_PITEM

-- ================================================== INSERT
INSERT INTO CZ_PR_ASSEMBLING_ITEM 
(
	CD_COMPANY,
	CD_PLANT,
	CD_ITEM,
	CD_PITEM,
	YN_MATCHING,
	CD_MITEM,
	ID_INSERT,
	DTS_INSERT
)
SELECT CD_COMPANY,
	   CD_PLANT,
	   CD_ITEM,
	   CD_PITEM,
	   YN_MATCHING,
	   CD_MITEM,
       @P_ID_USER, 
       NEOE.SF_SYSDATE(GETDATE()) 
FROM OPENXML (@DOC, '/XML/I', 2) 
        WITH (CD_COMPANY		NVARCHAR(7),
			  CD_PLANT			NVARCHAR(7),
			  CD_ITEM			NVARCHAR(20),
			  CD_PITEM			NVARCHAR(20),
			  YN_MATCHING		NVARCHAR(1),
			  CD_MITEM			NVARCHAR(20))

-- ================================================== UPDATE    
UPDATE A 
   SET A.YN_MATCHING = B.YN_MATCHING,
	   A.CD_MITEM = B.CD_MITEM,
	   A.ID_UPDATE = @P_ID_USER, 
	   A.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
FROM CZ_PR_ASSEMBLING_ITEM A 
JOIN OPENXML (@DOC, '/XML/U', 2) 
        WITH (CD_COMPANY		NVARCHAR(7),
			  CD_PLANT			NVARCHAR(7),
			  CD_ITEM			NVARCHAR(20),
			  CD_PITEM			NVARCHAR(20),
			  YN_MATCHING		NVARCHAR(1),
			  CD_MITEM			NVARCHAR(20)) B 
 ON A.CD_COMPANY = B.CD_COMPANY
AND A.CD_PLANT = B.CD_PLANT
AND A.CD_ITEM = B.CD_ITEM
AND A.CD_PITEM = B.CD_PITEM

EXEC SP_XML_REMOVEDOCUMENT @DOC 

GO

