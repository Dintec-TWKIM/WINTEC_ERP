ALTER PROCEDURE [NEOE].[UP_SA_SO_BOM_S]
(
	@P_CD_COMPANY        NVARCHAR(7),
	@P_CD_PLANT          NVARCHAR(7), 
    @P_CD_ITEM           NVARCHAR(20)
)
AS

SELECT	PB.CD_MATL, P.NM_ITEM, P.STND_ITEM, P.STND_DETAIL_ITEM, PB.QT_ITEM_NET AS QT_NEED_UNIT, PB.QT_ITEM AS QT_NEED,
		P.UNIT_IM, PB.NO_SEQ AS SEQ_SO_LINE
FROM	PR_BOM PB
		LEFT OUTER JOIN MA_PITEM P ON PB.CD_COMPANY = P.CD_COMPANY AND PB.CD_PLANT = P.CD_PLANT AND PB.CD_MATL = P.CD_ITEM
WHERE	PB.CD_COMPANY	= @P_CD_COMPANY
AND		PB.CD_PLANT		= @P_CD_PLANT
AND		PB.CD_ITEM		= @P_CD_ITEM
GO

