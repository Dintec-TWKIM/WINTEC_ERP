ALTER PROCEDURE [NEOE].[UP_SA_SO_UM_FIXED_S]
(
    @P_CD_COMPANY       NVARCHAR(7),
    @P_CD_PARTNER       NVARCHAR(20),
    @P_CD_PLANT         NVARCHAR(7),
    @P_MULTI_ITEM       NVARCHAR(4000)
)
AS

BEGIN
    SELECT  CD_PARTNER, CD_PLANT, CD_ITEM, UM_FIXED
    FROM    SA_UM_FIXED
    WHERE   CD_COMPANY  = @P_CD_COMPANY
    AND     CD_PARTNER  = @P_CD_PARTNER
    AND     CD_PLANT    = @P_CD_PLANT
    AND     CD_ITEM IN (SELECT CD_STR FROM GETTABLEFROMSPLIT(@P_MULTI_ITEM))
END
GO

