ALTER PROCEDURE [NEOE].[UP_SA_SO_PAYCOND_S]
(
	@P_CD_COMPANY       NVARCHAR(7),
	@P_NO_SO            NVARCHAR(20)
)
AS

SELECT  NO_SO, NO_LINE_PAYCOND, CD_PAYCOND, MONTH_PAYCOND, DAY_PAYCOND, RT_PAYCOND, DC_RMK
FROM    SA_SO_PAYCOND
WHERE   CD_COMPANY      = @P_CD_COMPANY
AND     NO_SO           = @P_NO_SO

RETURN
GO

