ALTER PROC [NEOE].[UP_CZ_PR_INV_SCH_ROUT_L]
(
	@P_CD_COMPANY			NVARCHAR(7),
	@P_CD_PLANT				NVARCHAR(7),
	@P_CD_ITEM				NVARCHAR(20),
	@P_CD_OP				NVARCHAR(4),
	@P_TXT_USERDEF5			nvarchar(200)
)
AS

UPDATE  PR_ROUT_L 
SET TXT_USERDEF5 = @P_TXT_USERDEF5
WHERE CD_COMPANY = @P_CD_COMPANY
AND CD_PLANT = @P_CD_PLANT
AND (CD_ITEM = @P_CD_ITEM OR @P_CD_ITEM = '' OR @P_CD_ITEM IS NULL)
AND (CD_OP = @P_CD_OP OR @P_CD_OP = '' OR @P_CD_OP IS NULL)


GO