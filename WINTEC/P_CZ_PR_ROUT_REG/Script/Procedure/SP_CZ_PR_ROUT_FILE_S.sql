ALTER PROCEDURE [NEOE].[SP_CZ_PR_ROUT_FILE_S]
(  
    @P_CD_COMPANY       NVARCHAR(7),
    @P_CD_PLANT         NVARCHAR(7),
	@P_CD_ITEM			NVARCHAR(20),
	@P_NO_OPPATH		NVARCHAR(3),
	@P_CD_OP			NVARCHAR(4),
	@P_CD_WCOP			NVARCHAR(7)
)  
AS

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT RF.CD_COMPANY,
	   RF.CD_PLANT,
	   RF.CD_ITEM,
	   RF.NO_OPPATH,
	   RF.CD_OP,
	   RF.CD_WCOP,
	   RF.NO_SEQ,
	   RF.NM_FILE,
	   RF.DC_FILE,
	   RF.ID_INSERT,
	   MU.NM_USER AS NM_INSERT,
	   RF.DTS_INSERT
FROM CZ_PR_ROUT_FILE RF
LEFT JOIN MA_USER MU ON MU.CD_COMPANY = RF.CD_COMPANY AND MU.ID_USER = RF.ID_INSERT
WHERE RF.CD_COMPANY = @P_CD_COMPANY
AND RF.CD_PLANT = @P_CD_PLANT
AND RF.CD_ITEM = @P_CD_ITEM
AND RF.NO_OPPATH = @P_NO_OPPATH
AND RF.CD_OP = @P_CD_OP
AND RF.CD_WCOP = @P_CD_WCOP