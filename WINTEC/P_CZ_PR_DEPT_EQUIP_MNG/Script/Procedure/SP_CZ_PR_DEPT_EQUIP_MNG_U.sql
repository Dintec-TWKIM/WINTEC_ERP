ALTER PROCEDURE [NEOE].[SP_CZ_PR_DEPT_EQUIP_MNG_U]
(
    @P_CD_COMPANY   NVARCHAR(7),
	@P_NO_WO		NVARCHAR(20),
	@P_NO_LINE		NUMERIC(5, 0),
	@P_CD_USERDEF1  NVARCHAR(4),
	@P_ID_UPDATE	NVARCHAR(15)
)
AS

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

UPDATE WR
SET WR.CD_USERDEF1 = @P_CD_USERDEF1,
	WR.ID_UPDATE = @P_ID_UPDATE,
    WR.DTS_UPDATE = NEOE.SF_SYSDATE(GETDATE())
FROM PR_WO_ROUT WR
WHERE WR.CD_COMPANY = @P_CD_COMPANY
AND NO_WO = @P_NO_WO
AND NO_LINE = @P_NO_LINE

GO

