ALTER PROC [NEOE].[UP_CZ_PR_OPOUT_PR_REG_DELETE_ALL]
(
    @P_CD_COMPANY   NVARCHAR(7),
    @P_CD_PLANT     NVARCHAR(7), 
    @P_NO_PR        NVARCHAR(20),
    @P_NM_PAGE		NVARCHAR(30) = NULL
)
AS

DECLARE @ERRMSG     NVARCHAR(255)

DELETE FROM CZ_PR_OPOUT_PR
WHERE CD_COMPANY = @P_CD_COMPANY
    AND CD_PLANT = @P_CD_PLANT
    AND NO_PR = @P_NO_PR

DELETE FROM CZ_PR_WO_INSP
WHERE CD_COMPANY = @P_CD_COMPANY
	AND NO_OPOUT_PR = @P_NO_PR
	AND NO_INSP = 994

IF (@@ERROR <> 0 ) BEGIN SELECT @ERRMSG = '[UP_CZ_PR_OPOUT_PR_REG_DELETE_ALL]작업을정상적으로처리하지못했습니다.' GOTO ERROR END


RETURN 
ERROR: RAISERROR (@ERRMSG, 18 , 1)
RETURN
