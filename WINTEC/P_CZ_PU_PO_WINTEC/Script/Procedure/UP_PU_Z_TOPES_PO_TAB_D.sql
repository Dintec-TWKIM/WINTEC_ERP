ALTER PROCEDURE [NEOE].[UP_PU_Z_TOPES_PO_TAB_D]  
(  
	@P_CD_COMPANY        NVARCHAR(7),   
	@P_NO_PO             NVARCHAR(20),    
	@P_NO_SEQ            NVARCHAR(20),
	@P_SERVER_KEY		 NVARCHAR(25)
)  
AS  
DECLARE     @ERRNO     INT,   
            @ERRMSG    NVARCHAR(255)  

  
DELETE PU_CUST_PUBLIC  
WHERE CD_NO_PK_1 = @P_CD_COMPANY
  AND CD_NO_PK_2 = @P_NO_PO
  AND CD_NO_PK_4 = @P_NO_SEQ
  AND NM_BUSINESS = 'P_PU_PO_REG2'
  AND CD_CUST_SERVER_KEY = @P_SERVER_KEY
  
IF (@@ERROR <> 0 ) BEGIN SELECT @ERRNO  = 100000, @ERRMSG = '작업을 정상적으로 처리하지 못했습니다.' GOTO ERROR END  
  
RETURN  
ERROR: RAISERROR (@ERRMSG , 18 ,1)
GO


