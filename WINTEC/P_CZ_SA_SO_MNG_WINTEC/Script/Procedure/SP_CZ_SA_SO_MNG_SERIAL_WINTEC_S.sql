ALTER PROCEDURE [NEOE].[SP_CZ_SA_SO_MNG_SERIAL_WINTEC_S]        
(  
    @P_CD_COMPANY           NVARCHAR(7),
    @P_NO_SO                NVARCHAR(20),
    @P_SEQ_SO               NUMERIC(5, 0)
)        
AS

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT SR.NO_SO,
       SR.SEQ_SO,
       SR.IDX,
       SR.NO_SERIAL 
FROM CZ_SA_SOL_SERIAL_WINTEC SR
WHERE SR.CD_COMPANY = @P_CD_COMPANY
AND SR.NO_SO = @P_NO_SO
AND SR.SEQ_SO = @P_SEQ_SO

GO

